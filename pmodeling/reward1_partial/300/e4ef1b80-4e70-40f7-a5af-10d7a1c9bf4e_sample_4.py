from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition

# Define the POWL model
A = Transition(label='Opportunity Scan')
B = Transition(label='Idea Workshop')
C = Transition(label='Concept Merge')
D = Transition(label='Resource Align')
E = Transition(label='Prototype Build')
F = Transition(label='Feasibility Test')
G = Transition(label='Pilot Launch')
H = Transition(label='Feedback Gather')
I = Transition(label='Design Adapt')
J = Transition(label='Compliance Check')
K = Transition(label='Scaling Plan')
L = Transition(label='IP Management')
M = Transition(label='Market Sync')
N = Transition(label='Partner Review')
O = Transition(label='Exit Strategy')
skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[A, B])
xor1 = OperatorPOWL(operator=Operator.XOR, children=[C, skip])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[D, E])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[F, skip])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[G, H])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[I, skip])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[J, K])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[L, skip])
loop5 = OperatorPOWL(operator=Operator.LOOP, children=[M, N])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[O, skip])

# Define the order of activities
root = StrictPartialOrder(nodes=[loop1, xor1, loop2, xor2, loop3, xor3, loop4, xor4, loop5, xor5])
root.order.add_edge(loop1, xor1)
root.order.add_edge(xor1, loop2)
root.order.add_edge(loop2, xor2)
root.order.add_edge(xor2, loop3)
root.order.add_edge(loop3, xor3)
root.order.add_edge(xor3, loop4)
root.order.add_edge(loop4, xor4)
root.order.add_edge(xor4, loop5)
root.order.add_edge(loop5, xor5)

print(root)