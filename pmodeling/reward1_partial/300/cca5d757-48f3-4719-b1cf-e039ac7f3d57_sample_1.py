import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_assess = Transition(label='Site Assess')
load_testing = Transition(label='Load Testing')
climate_study = Transition(label='Climate Study')
medium_prep = Transition(label='Medium Prep')
bed_install = Transition(label='Bed Install')
irrigation_setup = Transition(label='Irrigation Setup')
crop_select = Transition(label='Crop Select')
pest_control = Transition(label='Pest Control')
community_meet = Transition(label='Community Meet')
monitor_deploy = Transition(label='Monitor Deploy')
waste_cycle = Transition(label='Waste Cycle')
yield_forecast = Transition(label='Yield Forecast')
market_link = Transition(label='Market Link')
workshop_plan = Transition(label='Workshop Plan')
tech_integrate = Transition(label='Tech Integrate')

xor = OperatorPOWL(operator=Operator.XOR, children=[pest_control, community_meet])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[monitor_deploy, waste_cycle])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[yield_forecast, market_link])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[workshop_plan, tech_integrate])

root = StrictPartialOrder(nodes=[site_assess, load_testing, climate_study, medium_prep, bed_install, irrigation_setup, crop_select, xor, xor2, xor3, xor4])
root.order.add_edge(site_assess, load_testing)
root.order.add_edge(load_testing, climate_study)
root.order.add_edge(climate_study, medium_prep)
root.order.add_edge(medium_prep, bed_install)
root.order.add_edge(bed_install, irrigation_setup)
root.order.add_edge(irrigation_setup, crop_select)
root.order.add_edge(crop_select, xor)
root.order.add_edge(xor, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)