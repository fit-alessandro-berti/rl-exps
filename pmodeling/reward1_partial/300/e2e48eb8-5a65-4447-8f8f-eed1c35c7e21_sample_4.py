import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
structural_check = Transition(label='Structural Check')
climate_study = Transition(label='Climate Study')
soil_prep = Transition(label='Soil Prep')
seed_selection = Transition(label='Seed Selection')
irrigation_setup = Transition(label='Irrigation Setup')
nutrient_mix = Transition(label='Nutrient Mix')
sensor_install = Transition(label='Sensor Install')
pest_monitor = Transition(label='Pest Monitor')
data_analysis = Transition(label='Data Analysis')
regulation_review = Transition(label='Regulation Review')
community_meet = Transition(label='Community Meet')
harvest_plan = Transition(label='Harvest Plan')
packaging_design = Transition(label='Packaging Design')
distribution_map = Transition(label='Distribution Map')
feedback_loop = Transition(label='Feedback Loop')
maintenance_schedule = Transition(label='Maintenance Schedule')

skip = SilentTransition()

site_survey_xor = OperatorPOWL(operator=Operator.XOR, children=[structural_check, climate_study])
climate_xor = OperatorPOWL(operator=Operator.XOR, children=[soil_prep, skip])
soil_xor = OperatorPOWL(operator=Operator.XOR, children=[seed_selection, skip])
seed_xor = OperatorPOWL(operator=Operator.XOR, children=[irrigation_setup, nutrient_mix])
nutrient_xor = OperatorPOWL(operator=Operator.XOR, children=[sensor_install, pest_monitor])
pest_xor = OperatorPOWL(operator=Operator.XOR, children=[data_analysis, regulation_review])
data_xor = OperatorPOWL(operator=Operator.XOR, children=[community_meet, harvest_plan])
community_xor = OperatorPOWL(operator=Operator.XOR, children=[packaging_design, distribution_map])
packaging_xor = OperatorPOWL(operator=Operator.XOR, children=[feedback_loop, maintenance_schedule])

root = StrictPartialOrder(nodes=[site_survey, site_survey_xor, climate_xor, soil_xor, seed_xor, nutrient_xor, pest_xor, data_xor, community_xor, packaging_xor])
root.order.add_edge(site_survey, site_survey_xor)
root.order.add_edge(site_survey_xor, climate_xor)
root.order.add_edge(climate_xor, soil_xor)
root.order.add_edge(soil_xor, seed_xor)
root.order.add_edge(seed_xor, nutrient_xor)
root.order.add_edge(nutrient_xor, pest_xor)
root.order.add_edge(pest_xor, data_xor)
root.order.add_edge(data_xor, community_xor)
root.order.add_edge(community_xor, packaging_xor)