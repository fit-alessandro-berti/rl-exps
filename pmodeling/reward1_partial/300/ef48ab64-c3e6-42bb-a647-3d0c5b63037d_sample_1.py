from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition

A = Transition(label='Site Select')
B = Transition(label='Env Assess')
C = Transition(label='Design Modules')
D = Transition(label='Hydroponics Setup')
E = Transition(label='Software Dev')
F = Transition(label='Seed Choose')
G = Transition(label='LED Install')
H = Transition(label='Train Staff')
I = Transition(label='Compliance Check')
J = Transition(label='Engage Community')
K = Transition(label='Plant Crops')
L = Transition(label='Monitor Growth')
M = Transition(label='Optimize Yields')
N = Transition(label='Waste Manage')
O = Transition(label='Energy Audit')
P = Transition(label='Water Recycle')

skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[A, B, C, D])
xor1 = OperatorPOWL(operator=Operator.XOR, children=[E, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[F, xor1])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[G, H])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[I, xor2])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[J, xor3])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[K, xor3])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[L, xor4])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[M, xor4])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[N, xor5])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[O, xor6])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[P, xor7])
root = StrictPartialOrder(nodes=[loop1, xor2, loop2, xor3, loop3, xor4, loop4, xor5, xor6, xor7, xor8])
root.order.add_edge(loop1, xor2)
root.order.add_edge(xor2, loop2)
root.order.add_edge(loop2, xor3)
root.order.add_edge(xor3, loop3)
root.order.add_edge(loop3, xor4)
root.order.add_edge(xor4, loop4)
root.order.add_edge(loop4, xor5)
root.order.add_edge(xor5, xor6)
root.order.add_edge(xor6, xor7)
root.order.add_edge(xor7, xor8)

print(root)