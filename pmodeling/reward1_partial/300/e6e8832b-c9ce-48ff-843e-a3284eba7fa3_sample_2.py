from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

# Define the activities
asset_listing = Transition(label='Asset Listing')
valuation_check = Transition(label='Valuation Check')
compliance_scan = Transition(label='Compliance Scan')
legal_review = Transition(label='Legal Review')
remote_audit = Transition(label='Remote Audit')
auction_setup = Transition(label='Auction Setup')
bid_monitoring = Transition(label='Bid Monitoring')
fraud_detection = Transition(label='Fraud Detection')
ownership_transfer = Transition(label='Ownership Transfer')
payment_clearing = Transition(label='Payment Clearing')
tax_calculation = Transition(label='Tax Calculation')
fund_allocation = Transition(label='Fund Allocation')
dispute_handling = Transition(label='Dispute Handling')
report_generation = Transition(label='Report Generation')
stakeholder_update = Transition(label='Stakeholder Update')

# Define the process tree
xor1 = OperatorPOWL(operator=Operator.XOR, children=[compliance_scan, valuation_check])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[legal_review, remote_audit])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[auction_setup, bid_monitoring])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[fraud_detection, ownership_transfer])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[payment_clearing, tax_calculation])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[fund_allocation, dispute_handling])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[report_generation, stakeholder_update])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[xor1, xor2])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[xor3, xor4])
xor10 = OperatorPOWL(operator=Operator.XOR, children=[xor5, xor6])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[xor7, xor8])
xor12 = OperatorPOWL(operator=Operator.XOR, children=[xor9, xor10])
xor13 = OperatorPOWL(operator=Operator.XOR, children=[xor11, xor12])
xor14 = OperatorPOWL(operator=Operator.XOR, children=[xor13, asset_listing])

# Define the root of the POWL model
root = StrictPartialOrder(nodes=[xor14])
root.order.add_edge(xor14, xor1)
root.order.add_edge(xor14, xor2)
root.order.add_edge(xor1, xor3)
root.order.add_edge(xor2, xor4)
root.order.add_edge(xor3, xor5)
root.order.add_edge(xor4, xor6)
root.order.add_edge(xor5, xor7)
root.order.add_edge(xor6, xor8)
root.order.add_edge(xor7, xor9)
root.order.add_edge(xor8, xor10)
root.order.add_edge(xor9, xor11)
root.order.add_edge(xor10, xor12)
root.order.add_edge(xor11, xor13)
root.order.add_edge(xor12, xor14)

print(root)