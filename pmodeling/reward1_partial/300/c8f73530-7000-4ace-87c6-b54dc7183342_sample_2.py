import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

# Define the activities
milk_sourcing = Transition(label='Milk Sourcing')
quality_testing = Transition(label='Quality Testing')
milk_pasturize = Transition(label='Milk Pasteurize')
culture_inoculate = Transition(label='Culture Inoculate')
coagulation = Transition(label='Coagulation')
curd_cutting = Transition(label='Curd Cutting')
whey_drain = Transition(label='Whey Drain')
pressing = Transition(label='Pressing')
salting = Transition(label='Salting')
aging_control = Transition(label='Aging Control')
sensory_audit = Transition(label='Sensory Audit')
packaging_design = Transition(label='Packaging Design')
label_approval = Transition(label='Label Approval')
order_customization = Transition(label='Order Customization')
logistics_plan = Transition(label='Logistics Plan')
market_delivery = Transition(label='Market Delivery')
customer_feedback = Transition(label='Customer Feedback')
regulatory_check = Transition(label='Regulatory Check')

# Define the POWL model
loop_aging = OperatorPOWL(operator=Operator.LOOP, children=[aging_control, sensory_audit])
xor_customization = OperatorPOWL(operator=Operator.XOR, children=[order_customization, logistics_plan])
xor_logistics = OperatorPOWL(operator=Operator.XOR, children=[market_delivery, regulatory_check])
xor_feedback = OperatorPOWL(operator=Operator.XOR, children=[customer_feedback, label_approval])

root = StrictPartialOrder(nodes=[milk_sourcing, quality_testing, milk_pasturize, culture_inoculate, coagulation, curd_cutting, whey_drain, pressing, salting, loop_aging, xor_customization, xor_logistics, xor_feedback])
root.order.add_edge(milk_sourcing, quality_testing)
root.order.add_edge(quality_testing, milk_pasturize)
root.order.add_edge(milk_pasturize, culture_inoculate)
root.order.add_edge(culture_inoculate, coagulation)
root.order.add_edge(coagulation, curd_cutting)
root.order.add_edge(curd_cutting, whey_drain)
root.order.add_edge(whey_drain, pressing)
root.order.add_edge(pressing, salting)
root.order.add_edge(salting, loop_aging)
root.order.add_edge(loop_aging, xor_customization)
root.order.add_edge(xor_customization, xor_logistics)
root.order.add_edge(xor_logistics, xor_feedback)
root.order.add_edge(xor_feedback, market_delivery)
root.order.add_edge(market_delivery, regulatory_check)
root.order.add_edge(regulatory_check, customer_feedback)
root.order.add_edge(customer_feedback, label_approval)
root.order.add_edge(label_approval, order_customization)