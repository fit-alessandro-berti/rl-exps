compliance_audit = OperatorPOWL(operator=Operator.XOR, children=[compliance_check, contract_draft])

root = StrictPartialOrder(nodes=[farm_visit_audit, quality_audit, shipment_audit, inventory_audit, risk_audit, payment_audit, traceability_audit, compliance_audit])
root.order.add_edge(farm_visit_audit, quality_audit)
