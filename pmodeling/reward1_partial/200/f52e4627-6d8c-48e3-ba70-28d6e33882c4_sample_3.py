root.order.add_edge(farm_visit_audit, payment_audit)
root.order.add_edge(farm_visit_audit, traceability_audit)
root.order.add_edge(farm_visit_audit, compliance_audit)
root.order.add_edge(quality_audit, shipment_audit)
