from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_review = Transition(label='Site Review')
impact_study = Transition(label='Impact Study')
design_plan = Transition(label='Design Plan')
structure_mod = Transition(label='Structure Mod')
hydroponics_setup = Transition(label='Hydroponics Setup')
crop_select = Transition(label='Crop Select')
nutrient_mix = Transition(label='Nutrient Mix')
pest_control = Transition(label='Pest Control')
sensor_install = Transition(label='Sensor Install')
staff_train = Transition(label='Staff Train')
compliance_audit = Transition(label='Compliance Audit')
packaging_dev = Transition(label='Packaging Dev')
logistics_plan = Transition(label='Logistics Plan')
community_engage = Transition(label='Community Engage')
sustainability_check = Transition(label='Sustainability Check')

skip = SilentTransition()

site_review_to_impact_study = OperatorPOWL(operator=Operator.XOR, children=[site_review, impact_study])
impact_study_to_design_plan = OperatorPOWL(operator=Operator.XOR, children=[impact_study, design_plan])
design_plan_to_structure_mod = OperatorPOWL(operator=Operator.XOR, children=[design_plan, structure_mod])
structure_mod_to_hydroponics_setup = OperatorPOWL(operator=Operator.XOR, children=[structure_mod, hydroponics_setup])
hydroponics_setup_to_crop_select = OperatorPOWL(operator=Operator.XOR, children=[hydroponics_setup, crop_select])
crop_select_to_nutrient_mix = OperatorPOWL(operator=Operator.XOR, children=[crop_select, nutrient_mix])
nutrient_mix_to_pest_control = OperatorPOWL(operator=Operator.XOR, children=[nutrient_mix, pest_control])
pest_control_to_sensor_install = OperatorPOWL(operator=Operator.XOR, children=[pest_control, sensor_install])
sensor_install_to_staff_train = OperatorPOWL(operator=Operator.XOR, children=[sensor_install, staff_train])
staff_train_to_compliance_audit = OperatorPOWL(operator=Operator.XOR, children=[staff_train, compliance_audit])
compliance_audit_to_packaging_dev = OperatorPOWL(operator=Operator.XOR, children=[compliance_audit, packaging_dev])
packaging_dev_to_logistics_plan = OperatorPOWL(operator=Operator.XOR, children=[packaging_dev, logistics_plan])
logistics_plan_to_community_engage = OperatorPOWL(operator=Operator.XOR, children=[logistics_plan, community_engage])
community_engage_to_sustainability_check = OperatorPOWL(operator=Operator.XOR, children=[community_engage, sustainability_check])

root = StrictPartialOrder(nodes=[site_review, impact_study, design_plan, structure_mod, hydroponics_setup, crop_select, nutrient_mix, pest_control, sensor_install, staff_train, compliance_audit, packaging_dev, logistics_plan, community_engage, sustainability_check])
root.order.add_edge(site_review, impact_study)
root.order.add_edge(impact_study, design_plan)
root.order.add_edge(design_plan, structure_mod)
root.order.add_edge(structure_mod, hydroponics_setup)
root.order.add_edge(hydroponics_setup, crop_select)
root.order.add_edge(crop_select, nutrient_mix)
root.order.add_edge(nutrient_mix, pest_control)
root.order.add_edge(pest_control, sensor_install)
root.order.add_edge(sensor_install, staff_train)
root.order.add_edge(staff_train, compliance_audit)
root.order.add_edge(compliance_audit, packaging_dev)
root.order.add_edge(packaging_dev, logistics_plan)
root.order.add_edge(logistics_plan, community_engage)
root.order.add_edge(community_engage, sustainability_check)