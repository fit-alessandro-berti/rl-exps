import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

inquiry = Transition(label='Inquiry Intake')
consultation = Transition(label='Consultation Call')
concept = Transition(label='Concept Draft')
feedback = Transition(label='Feedback Loop')
contract = Transition(label='Contract Setup')
artist = Transition(label='Artist Match')
sourcing = Transition(label='Material Sourcing')
ethics = Transition(label='Ethics Review')
progress = Transition(label='Progress Check')
milestone = Transition(label='Milestone Approve')
quality = Transition(label='Quality Audit')
copyright = Transition(label='Copyright Transfer')
packaging = Transition(label='Packaging Plan')
shipping = Transition(label='Shipping Arrange')
post_delivery = Transition(label='Post Delivery')
survey = Transition(label='Client Survey')

skip = SilentTransition()
xor = OperatorPOWL(operator=Operator.XOR, children=[feedback, contract])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[artist, sourcing])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[ethics, progress])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[milestone, quality])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[copyright, packaging])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[shipping, post_delivery])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[survey, skip])

root = StrictPartialOrder(nodes=[inquiry, consultation, concept, xor, xor2, xor3, xor4, xor5, xor6, xor7])
root.order.add_edge(inquiry, consultation)
root.order.add_edge(inquiry, concept)
root.order.add_edge(consultation, xor)
root.order.add_edge(concept, xor)
root.order.add_edge(xor, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, xor5)
root.order.add_edge(xor5, xor6)
root.order.add_edge(xor6, xor7)
root.order.add_edge(xor7, survey)

print(root)