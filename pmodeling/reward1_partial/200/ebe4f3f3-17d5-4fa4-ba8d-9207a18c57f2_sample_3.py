skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Milk_Sourcing, Farm_Audit, Milk_Testing, Batch_Forming, Curd_Cutting, Molding_Cheese, Salting_Process, Aging_Control, Quality_Check, Packaging_Design, Label_Printing, Inventory_Update, Order_Receiving, Retail_Coordination, Shipping_Prep, Customer_Feedback, Demand_Forecast, Limited_Release])
xor = OperatorPOWL(operator=Operator.XOR, children=[skip])
root = StrictPartialOrder(nodes=[loop, xor])
root.order.add_edge(loop, xor)