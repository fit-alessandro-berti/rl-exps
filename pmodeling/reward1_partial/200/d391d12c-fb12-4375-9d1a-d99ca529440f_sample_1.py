import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
climate_check = Transition(label='Climate Check')
soil_testing = Transition(label='Soil Testing')
media_select = Transition(label='Media Select')
design_layout = Transition(label='Design Layout')
hydro_setup = Transition(label='Hydro Setup')
nutrient_mix = Transition(label='Nutrient Mix')
sensor_install = Transition(label='Sensor Install')
regulation_review = Transition(label='Regulation Review')
permit_apply = Transition(label='Permit Apply')
stakeholder_meet = Transition(label='Stakeholder Meet')
community_train = Transition(label='Community Train')
plant_seed = Transition(label='Plant Seed')
monitor_growth = Transition(label='Monitor Growth')
adjust_controls = Transition(label='Adjust Controls')
harvest_plan = Transition(label='Harvest Plan')
waste_recycle = Transition(label='Waste Recycle')
feedback_loop = Transition(label='Feedback Loop')

# Define the relationships between activities
site_survey = OperatorPOWL(operator=Operator.XOR, children=[climate_check, soil_testing])
climate_check = OperatorPOWL(operator=Operator.LOOP, children=[media_select, design_layout])
media_select = OperatorPOWL(operator=Operator.LOOP, children=[hydro_setup, nutrient_mix])
nutrient_mix = OperatorPOWL(operator=Operator.LOOP, children=[sensor_install, regulation_review])
sensor_install = OperatorPOWL(operator=Operator.LOOP, children=[permit_apply, stakeholder_meet])
regulation_review = OperatorPOWL(operator=Operator.LOOP, children=[community_train, plant_seed])
permit_apply = OperatorPOWL(operator=Operator.LOOP, children=[monitor_growth, adjust_controls])
community_train = OperatorPOWL(operator=Operator.LOOP, children=[harvest_plan, waste_recycle])
plant_seed = OperatorPOWL(operator=Operator.LOOP, children=[feedback_loop])

root = StrictPartialOrder(nodes=[site_survey, climate_check, media_select, nutrient_mix, sensor_install, regulation_review, permit_apply, stakeholder_meet, community_train, plant_seed, monitor_growth, adjust_controls, harvest_plan, waste_recycle, feedback_loop])
root.order.add_edge(site_survey, climate_check)
root.order.add_edge(climate_check, media_select)
root.order.add_edge(media_select, hydro_setup)
root.order.add_edge(hydro_setup, nutrient_mix)
root.order.add_edge(nutrient_mix, sensor_install)
root.order.add_edge(sensor_install, regulation_review)
root.order.add_edge(regulation_review, permit_apply)
root.order.add_edge(permit_apply, stakeholder_meet)
root.order.add_edge(stakeholder_meet, community_train)
root.order.add_edge(community_train, plant_seed)
root.order.add_edge(plant_seed, monitor_growth)
root.order.add_edge(monitor_growth, adjust_controls)
root.order.add_edge(adjust_controls, harvest_plan)
root.order.add_edge(harvest_plan, waste_recycle)
root.order.add_edge(waste_recycle, feedback_loop)