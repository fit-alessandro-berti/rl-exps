import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
Site_Survey = Transition(label='Site Survey')
Permit_Review = Transition(label='Permit Review')
Design_Layout = Transition(label='Design Layout')
Material_Sourcing = Transition(label='Material Sourcing')
Irrigation_Setup = Transition(label='Irrigation Setup')
Sensor_Install = Transition(label='Sensor Install')
Structural_Test = Transition(label='Structural Test')
Recruit_Farmers = Transition(label='Recruit Farmers')
Trial_Planting = Transition(label='Trial Planting')
Pest_Control = Transition(label='Pest Control')
Soilless_Prep = Transition(label='Soilless Prep')
System_Calibrate = Transition(label='System Calibrate')
Data_Monitor = Transition(label='Data Monitor')
Harvest_Plan = Transition(label='Harvest Plan')
Community_Outreach = Transition(label='Community Outreach')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Site_Survey, Permit_Review, Design_Layout, Material_Sourcing, Irrigation_Setup, Sensor_Install, Structural_Test, Recruit_Farmers, Trial_Planting, Pest_Control, Soilless_Prep, System_Calibrate, Data_Monitor, Harvest_Plan, Community_Outreach])
xor = OperatorPOWL(operator=Operator.XOR, children=[skip])
root = StrictPartialOrder(nodes=[loop, xor])
root.order.add_edge(loop, xor)