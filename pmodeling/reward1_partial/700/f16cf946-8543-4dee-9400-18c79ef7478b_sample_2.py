import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
climate_study = Transition(label='Climate Study')
system_design = Transition(label='System Design')
seed_selection = Transition(label='Seed Selection')
unit_install = Transition(label='Unit Install')
sensor_setup = Transition(label='Sensor Setup')
nutrient_mix = Transition(label='Nutrient Mix')
energy_audit = Transition(label='Energy Audit')
pest_control = Transition(label='Pest Control')
crop_plan = Transition(label='Crop Plan')
quality_check = Transition(label='Quality Check')
yield_forecast = Transition(label='Yield Forecast')
supply_sync = Transition(label='Supply Sync')
staff_train = Transition(label='Staff Train')
data_review = Transition(label='Data Review')

site_survey_loop = OperatorPOWL(operator=Operator.LOOP, children=[site_survey, climate_study])
climate_study_loop = OperatorPOWL(operator=Operator.LOOP, children=[system_design, seed_selection])
seed_selection_loop = OperatorPOWL(operator=Operator.LOOP, children=[unit_install, sensor_setup])
unit_install_loop = OperatorPOWL(operator=Operator.LOOP, children=[nutrient_mix, energy_audit])
nutrient_mix_loop = OperatorPOWL(operator=Operator.LOOP, children=[pest_control, crop_plan])
pest_control_loop = OperatorPOWL(operator=Operator.LOOP, children=[quality_check, yield_forecast])
quality_check_loop = OperatorPOWL(operator=Operator.LOOP, children=[supply_sync, staff_train])
supply_sync_loop = OperatorPOWL(operator=Operator.LOOP, children=[data_review, site_survey])

root = StrictPartialOrder(nodes=[site_survey_loop, climate_study_loop, seed_selection_loop, unit_install_loop, nutrient_mix_loop, pest_control_loop, quality_check_loop, supply_sync_loop])
root.order.add_edge(site_survey_loop, climate_study_loop)
root.order.add_edge(climate_study_loop, system_design)
root.order.add_edge(system_design, seed_selection)
root.order.add_edge(seed_selection, unit_install)
root.order.add_edge(unit_install, nutrient_mix)
root.order.add_edge(nutrient_mix, energy_audit)
root.order.add_edge(energy_audit, pest_control)
root.order.add_edge(pest_control, crop_plan)
root.order.add_edge(crop_plan, quality_check)
root.order.add_edge(quality_check, supply_sync)
root.order.add_edge(supply_sync, data_review)
root.order.add_edge(data_review, site_survey)