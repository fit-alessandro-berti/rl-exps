import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

# Define the POWL model
milk_selection = Transition(label='Milk Selection')
quality_testing = Transition(label='Quality Testing')
milk_pasturize = Transition(label='Milk Pasteurize')
cheese_crafting = Transition(label='Cheese Crafting')
controlled_aging = Transition(label='Controlled Aging')
sensory_review = Transition(label='Sensory Review')
custom_packaging = Transition(label='Custom Packaging')
label_printing = Transition(label='Label Printing')
export_licensing = Transition(label='Export Licensing')
documentation_prep = Transition(label='Documentation Prep')
customs_clearance = Transition(label='Customs Clearance')
cold_shipping = Transition(label='Cold Shipping')
delivery_tracking = Transition(label='Delivery Tracking')
feedback_review = Transition(label='Feedback Review')
market_analysis = Transition(label='Market Analysis')

# Define the POWL model
milk_selection_to_quality_testing = OperatorPOWL(operator=Operator.AND, children=[milk_selection, quality_testing])
quality_testing_to_milk_pasturize = OperatorPOWL(operator=Operator.AND, children=[quality_testing, milk_pasturize])
milk_pasturize_to_cheese_crafting = OperatorPOWL(operator=Operator.AND, children=[milk_pasturize, cheese_crafting])
cheese_crafting_to_controlled_aging = OperatorPOWL(operator=Operator.AND, children=[cheese_crafting, controlled_aging])
controlled_aging_to_sensory_review = OperatorPOWL(operator=Operator.AND, children=[controlled_aging, sensory_review])
sensory_review_to_custom_packaging = OperatorPOWL(operator=Operator.AND, children=[sensory_review, custom_packaging])
custom_packaging_to_label_printing = OperatorPOWL(operator=Operator.AND, children=[custom_packaging, label_printing])
label_printing_to_export_licensing = OperatorPOWL(operator=Operator.AND, children=[label_printing, export_licensing])
export_licensing_to_documentation_prep = OperatorPOWL(operator=Operator.AND, children=[export_licensing, documentation_prep])
documentation_prep_to_customs_clearance = OperatorPOWL(operator=Operator.AND, children=[documentation_prep, customs_clearance])
customs_clearance_to_cold_shipping = OperatorPOWL(operator=Operator.AND, children=[customs_clearance, cold_shipping])
cold_shipping_to_delivery_tracking = OperatorPOWL(operator=Operator.AND, children=[cold_shipping, delivery_tracking])
delivery_tracking_to_feedback_review = OperatorPOWL(operator=Operator.AND, children=[delivery_tracking, feedback_review])
feedback_review_to_market_analysis = OperatorPOWL(operator=Operator.AND, children=[feedback_review, market_analysis])

# Define the partial order
root = StrictPartialOrder(nodes=[milk_selection, quality_testing, milk_pasturize, cheese_crafting, controlled_aging, sensory_review, custom_packaging, label_printing, export_licensing, documentation_prep, customs_clearance, cold_shipping, delivery_tracking, feedback_review, market_analysis])
root.order.add_edge(milk_selection, quality_testing)
root.order.add_edge(quality_testing, milk_pasturize)
root.order.add_edge(milk_pasturize, cheese_crafting)
root.order.add_edge(cheese_crafting, controlled_aging)
root.order.add_edge(controlled_aging, sensory_review)
root.order.add_edge(sensory_review, custom_packaging)
root.order.add_edge(custom_packaging, label_printing)
root.order.add_edge(label_printing, export_licensing)
root.order.add_edge(export_licensing, documentation_prep)
root.order.add_edge(documentation_prep, customs_clearance)
root.order.add_edge(customs_clearance, cold_shipping)
root.order.add_edge(cold_shipping, delivery_tracking)
root.order.add_edge(delivery_tracking, feedback_review)
root.order.add_edge(feedback_review, market_analysis)