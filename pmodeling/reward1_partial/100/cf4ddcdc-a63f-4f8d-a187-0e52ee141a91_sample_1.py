import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition

# Define the POWL model
A = Transition(label='Return Request')
B = Transition(label='Authorization Check')
C = Transition(label='Pickup Schedule')
D = Transition(label='Transport Dispatch')
E = Transition(label='Receiving Goods')
F = Transition(label='Quality Inspect')
G = Transition(label='Sort Items')
H = Transition(label='Refurbish Prep')
I = Transition(label='Recycle Process')
J = Transition(label='Inventory Update')
K = Transition(label='Customer Notify')
L = Transition(label='Disposal Arrange')
M = Transition(label='Compliance Audit')
N = Transition(label='Cost Analysis')
O = Transition(label='Report Generate')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[A, B])
xor = OperatorPOWL(operator=Operator.XOR, children=[C, skip])
root = StrictPartialOrder(nodes=[loop, xor, D, E, F, G, H, I, J, K, L, M, N, O])
root.order.add_edge(loop, xor)
root.order.add_edge(xor, D)
root.order.add_edge(D, E)
root.order.add_edge(E, F)
root.order.add_edge(F, G)
root.order.add_edge(G, H)
root.order.add_edge(H, I)
root.order.add_edge(I, J)
root.order.add_edge(J, K)
root.order.add_edge(K, L)
root.order.add_edge(L, M)
root.order.add_edge(M, N)
root.order.add_edge(N, O)

# Print the POWL model
print(root)