import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
load_test = Transition(label='Load Test')
soil_sample = Transition(label='Soil Sample')
water_check = Transition(label='Water Check')
design_plan = Transition(label='Design Plan')
bed_setup = Transition(label='Bed Setup')
irrigation_install = Transition(label='Irrigation Install')
climate_setup = Transition(label='Climate Setup')
seed_selection = Transition(label='Seed Selection')
planting_phase = Transition(label='Planting Phase')
pest_control = Transition(label='Pest Control')
growth_monitor = Transition(label='Growth Monitor')
harvest_prep = Transition(label='Harvest Prep')
community_meet = Transition(label='Community Meet')
waste_manage = Transition(label='Waste Manage')
yield_report = Transition(label='Yield Report')

xor1 = OperatorPOWL(operator=Operator.XOR, children=[design_plan, site_survey])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[load_test, soil_sample])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[water_check, climate_setup])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[seed_selection, planting_phase])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[pest_control, growth_monitor])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[harvest_prep, community_meet])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[waste_manage, yield_report])

root = StrictPartialOrder(nodes=[xor1, xor2, xor3, xor4, xor5, xor6, xor7])
root.order.add_edge(xor1, xor2)
root.order.add_edge(xor1, xor3)
root.order.add_edge(xor2, xor4)
root.order.add_edge(xor2, xor5)
root.order.add_edge(xor3, xor6)
root.order.add_edge(xor3, xor7)
root.order.add_edge(xor4, xor6)
root.order.add_edge(xor4, xor7)
root.order.add_edge(xor5, xor6)
root.order.add_edge(xor5, xor7)
root.order.add_edge(xor6, yield_report)
root.order.add_edge(xor7, yield_report)