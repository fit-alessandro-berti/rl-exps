from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition

site_survey = Transition(label='Site Survey')
load_test = Transition(label='Load Test')
permit_review = Transition(label='Permit Review')
design_layout = Transition(label='Design Layout')
material_sourcing = Transition(label='Material Sourcing')
soil_prep = Transition(label='Soil Prep')
hydroponic_setup = Transition(label='Hydroponic Setup')
community_meet = Transition(label='Community Meet')
crop_select = Transition(label='Crop Select')
sensor_install = Transition(label='Sensor Install')
water_testing = Transition(label='Water Testing')
pest_control = Transition(label='Pest Control')
growth_monitor = Transition(label='Growth Monitor')
harvest_plan = Transition(label='Harvest Plan')
market_launch = Transition(label='Market Launch')
feedback_collect = Transition(label='Feedback Collect')

site_survey_loop = OperatorPOWL(operator=Operator.LOOP, children=[site_survey, load_test, permit_review])
material_sourcing_loop = OperatorPOWL(operator=Operator.LOOP, children=[material_sourcing])
soil_prep_loop = OperatorPOWL(operator=Operator.LOOP, children=[soil_prep])
hydroponic_setup_loop = OperatorPOWL(operator=Operator.LOOP, children=[hydroponic_setup])
sensor_install_loop = OperatorPOWL(operator=Operator.LOOP, children=[sensor_install])
pest_control_loop = OperatorPOWL(operator=Operator.LOOP, children=[pest_control, growth_monitor])
harvest_plan_loop = OperatorPOWL(operator=Operator.LOOP, children=[harvest_plan])
market_launch_loop = OperatorPOWL(operator=Operator.LOOP, children=[market_launch])
feedback_collect_loop = OperatorPOWL(operator=Operator.LOOP, children=[feedback_collect])

root = StrictPartialOrder(nodes=[site_survey_loop, material_sourcing_loop, soil_prep_loop, hydroponic_setup_loop, sensor_install_loop, pest_control_loop, harvest_plan_loop, market_launch_loop, feedback_collect_loop])
root.order.add_edge(site_survey_loop, material_sourcing_loop)
root.order.add_edge(material_sourcing_loop, soil_prep_loop)
root.order.add_edge(soil_prep_loop, hydroponic_setup_loop)
root.order.add_edge(hydroponic_setup_loop, sensor_install_loop)
root.order.add_edge(sensor_install_loop, pest_control_loop)
root.order.add_edge(pest_control_loop, growth_monitor)
root.order.add_edge(growth_monitor, harvest_plan_loop)
root.order.add_edge(harvest_plan_loop, market_launch_loop)
root.order.add_edge(market_launch_loop, feedback_collect_loop)

print(root)