from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition

# Define the transitions for each activity
concept_approve = Transition(label='Concept Approve')
budget_align = Transition(label='Budget Align')
design_review = Transition(label='Design Review')
structure_simulate = Transition(label='Structure Simulate')
material_procure = Transition(label='Material Procure')
vendor_select = Transition(label='Vendor Select')
permit_apply = Transition(label='Permit Apply')
safety_check = Transition(label='Safety Check')
site_prep = Transition(label='Site Prep')
logistics_plan = Transition(label='Logistics Plan')
fabricate_parts = Transition(label='Fabricate Parts')
assemble_onsite = Transition(label='Assemble Onsite')
quality_inspect = Transition(label='Quality Inspect')
weather_monitor = Transition(label='Weather Monitor')
public_unveil = Transition(label='Public Unveil')
maintenance_plan = Transition(label='Maintenance Plan')
stakeholder_meet = Transition(label='Stakeholder Meet')

# Define the loop and choice nodes
loop_install = OperatorPOWL(operator=Operator.LOOP, children=[assemble_onsite, weather_monitor])
choice_inspect = OperatorPOWL(operator=Operator.XOR, children=[quality_inspect, stakeholder_meet])
choice_safety = OperatorPOWL(operator=Operator.XOR, children=[safety_check, permit_apply])

# Construct the POWL model
root = StrictPartialOrder(nodes=[concept_approve, budget_align, design_review, structure_simulate, material_procure, vendor_select, choice_safety, permit_apply, site_prep, logistics_plan, loop_install, weather_monitor, public_unveil, maintenance_plan, choice_inspect])
root.order.add_edge(concept_approve, budget_align)
root.order.add_edge(budget_align, design_review)
root.order.add_edge(design_review, structure_simulate)
root.order.add_edge(structure_simulate, material_procure)
root.order.add_edge(material_procure, vendor_select)
root.order.add_edge(vendor_select, choice_safety)
root.order.add_edge(permit_apply, choice_safety)
root.order.add_edge(choice_safety, site_prep)
root.order.add_edge(site_prep, logistics_plan)
root.order.add_edge(logistics_plan, loop_install)
root.order.add_edge(loop_install, weather_monitor)
root.order.add_edge(weather_monitor, public_unveil)
root.order.add_edge(public_unveil, maintenance_plan)
root.order.add_edge(maintenance_plan, choice_inspect)
root.order.add_edge(choice_inspect, quality_inspect)
root.order.add_edge(choice_inspect, stakeholder_meet)