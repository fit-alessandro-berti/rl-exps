import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_analysis = Transition(label='Site Analysis')
structural_check = Transition(label='Structural Check')
system_design = Transition(label='System Design')
hydroponics_setup = Transition(label='Hydroponics Setup')
lighting_install = Transition(label='Lighting Install')
sensor_install = Transition(label='Sensor Install')
climate_control = Transition(label='Climate Control')
nutrient_monitor = Transition(label='Nutrient Monitor')
seed_selection = Transition(label='Seed Selection')
germination_start = Transition(label='Germination Start')
auto_planting = Transition(label='Auto Planting')
growth_monitoring = Transition(label='Growth Monitoring')
ai_analytics = Transition(label='AI Analytics')
robotic_harvest = Transition(label='Robotic Harvest')
packaging = Transition(label='Packaging')
trace_labeling = Transition(label='Trace Labeling')
logistics_plan = Transition(label='Logistics Plan')

# Define the process model
site_analysis_to_structural_check = OperatorPOWL(operator=Operator.XOR, children=[site_analysis, structural_check])
structural_check_to_system_design = OperatorPOWL(operator=Operator.XOR, children=[structural_check, system_design])
system_design_to_hydroponics_setup = OperatorPOWL(operator=Operator.XOR, children=[system_design, hydroponics_setup])
hydroponics_setup_to_lighting_install = OperatorPOWL(operator=Operator.XOR, children=[hydroponics_setup, lighting_install])
lighting_install_to_sensor_install = OperatorPOWL(operator=Operator.XOR, children=[lighting_install, sensor_install])
sensor_install_to_climate_control = OperatorPOWL(operator=Operator.XOR, children=[sensor_install, climate_control])
climate_control_to_nutrient_monitor = OperatorPOWL(operator=Operator.XOR, children=[climate_control, nutrient_monitor])
nutrient_monitor_to_seed_selection = OperatorPOWL(operator=Operator.XOR, children=[nutrient_monitor, seed_selection])
seed_selection_to_germination_start = OperatorPOWL(operator=Operator.XOR, children=[seed_selection, germination_start])
germination_start_to_auto_planting = OperatorPOWL(operator=Operator.XOR, children=[germination_start, auto_planting])
auto_planting_to_growth_monitoring = OperatorPOWL(operator=Operator.XOR, children=[auto_planting, growth_monitoring])
growth_monitoring_to_ai_analytics = OperatorPOWL(operator=Operator.XOR, children=[growth_monitoring, ai_analytics])
ai_analytics_to_robotic_harvest = OperatorPOWL(operator=Operator.XOR, children=[ai_analytics, robotic_harvest])
robotic_harvest_to_packaging = OperatorPOWL(operator=Operator.XOR, children=[robotic_harvest, packaging])
packaging_to_trace_labeling = OperatorPOWL(operator=Operator.XOR, children=[packaging, trace_labeling])
trace_labeling_to_logistics_plan = OperatorPOWL(operator=Operator.XOR, children=[trace_labeling, logistics_plan])

# Create the root node
root = StrictPartialOrder(nodes=[site_analysis_to_structural_check, structural_check_to_system_design, system_design_to_hydroponics_setup, hydroponics_setup_to_lighting_install, lighting_install_to_sensor_install, sensor_install_to_climate_control, climate_control_to_nutrient_monitor, nutrient_monitor_to_seed_selection, seed_selection_to_germination_start, germination_start_to_auto_planting, auto_planting_to_growth_monitoring, growth_monitoring_to_ai_analytics, ai_analytics_to_robotic_harvest, robotic_harvest_to_packaging, packaging_to_trace_labeling, trace_labeling_to_logistics_plan])
root.order.add_edge(site_analysis_to_structural_check, structural_check_to_system_design)
root.order.add_edge(structural_check_to_system_design, system_design_to_hydroponics_setup)
root.order.add_edge(system_design_to_hydroponics_setup, hydroponics_setup_to_lighting_install)
root.order.add_edge(hydroponics_setup_to_lighting_install, lighting_install_to_sensor_install)
root.order.add_edge(lighting_install_to_sensor_install, sensor_install_to_climate_control)
root.order.add_edge(sensor_install_to_climate_control, climate_control_to_nutrient_monitor)
root.order.add_edge(climate_control_to_nutrient_monitor, nutrient_monitor_to_seed_selection)
root.order.add_edge(nutrient_monitor_to_seed_selection, seed_selection_to_germination_start)
root.order.add_edge(seed_selection_to_germination_start, germination_start_to_auto_planting)
root.order.add_edge(germination_start_to_auto_planting, auto_planting_to_growth_monitoring)
root.order.add_edge(auto_planting_to_growth_monitoring, growth_monitoring_to_ai_analytics)
root.order.add_edge(growth_monitoring_to_ai_analytics, ai_analytics_to_robotic_harvest)
root.order.add_edge(ai_analytics_to_robotic_harvest, robotic_harvest_to_packaging)
root.order.add_edge(robotic_harvest_to_packaging, packaging_to_trace_labeling)
root.order.add_edge(packaging_to_trace_labeling, trace_labeling_to_logistics_plan)

# Print the root node
print(root)