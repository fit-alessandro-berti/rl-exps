import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

artifact_intake = Transition(label='Artifact Intake')
catalog_entry = Transition(label='Catalog Entry')
visual_inspect = Transition(label='Visual Inspect')
material_test = Transition(label='Material Test')
spectroscopy = Transition(label='Spectroscopy')
historical_check = Transition(label='Historical Check')
provenance_trace = Transition(label='Provenance Trace')
style_compare = Transition(label='Style Compare')
three_d_scanning = Transition(label='3D Scanning')
condition_assess = Transition(label='Condition Assess')
preservation_plan = Transition(label='Preservation Plan')
legal_review = Transition(label='Legal Review')
report_draft = Transition(label='Report Draft')
report_finalize = Transition(label='Report Finalize')
archive_data = Transition(label='Archive Data')
sale_prep = Transition(label='Sale Prep')

skip = SilentTransition()

visual_inspect_loop = OperatorPOWL(operator=Operator.LOOP, children=[visual_inspect])
material_test_loop = OperatorPOWL(operator=Operator.LOOP, children=[material_test])
spectroscopy_loop = OperatorPOWL(operator=Operator.LOOP, children=[spectroscopy])
historical_check_loop = OperatorPOWL(operator=Operator.LOOP, children=[historical_check])
provenance_trace_loop = OperatorPOWL(operator=Operator.LOOP, children=[provenance_trace])
style_compare_loop = OperatorPOWL(operator=Operator.LOOP, children=[style_compare])
three_d_scanning_loop = OperatorPOWL(operator=Operator.LOOP, children=[three_d_scanning])
condition_assess_loop = OperatorPOWL(operator=Operator.LOOP, children=[condition_assess])

root = StrictPartialOrder(nodes=[artifact_intake, catalog_entry, visual_inspect_loop, material_test_loop, spectroscopy_loop, historical_check_loop, provenance_trace_loop, style_compare_loop, three_d_scanning_loop, condition_assess_loop, legal_review, report_draft, report_finalize, archive_data, sale_prep])
root.order.add_edge(artifact_intake, catalog_entry)
root.order.add_edge(catalog_entry, visual_inspect)
root.order.add_edge(visual_inspect, visual_inspect_loop)
root.order.add_edge(visual_inspect_loop, material_test)
root.order.add_edge(material_test, material_test_loop)
root.order.add_edge(material_test_loop, spectroscopy)
root.order.add_edge(spectroscopy, spectroscopy_loop)
root.order.add_edge(spectroscopy_loop, historical_check)
root.order.add_edge(historical_check, historical_check_loop)
root.order.add_edge(historical_check_loop, provenance_trace)
root.order.add_edge(provenance_trace, provenance_trace_loop)
root.order.add_edge(provenance_trace_loop, style_compare)
root.order.add_edge(style_compare, style_compare_loop)
root.order.add_edge(style_compare_loop, three_d_scanning)
root.order.add_edge(three_d_scanning, three_d_scanning_loop)
root.order.add_edge(three_d_scanning_loop, condition_assess)
root.order.add_edge(condition_assess, condition_assess_loop)
root.order.add_edge(condition_assess_loop, legal_review)
root.order.add_edge(legal_review, report_draft)
root.order.add_edge(report_draft, report_finalize)
root.order.add_edge(report_finalize, archive_data)
root.order.add_edge(archive_data, sale_prep)