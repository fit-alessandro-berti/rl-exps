root.order.add_edge(humidity_control_to_flavor_testing, flavor_testing_to_packaging_design)
root.order.add_edge(flavor_testing_to_packaging_design, packaging_design_to_order_processing)
root.order.add_edge(packaging_design_to_order_processing, order_processing_to_retail_delivery)
root.order.add_edge(order_processing_to_retail_delivery, retail_delivery_to_event_coordination)
