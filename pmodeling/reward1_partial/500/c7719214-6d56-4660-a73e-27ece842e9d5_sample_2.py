retail_delivery_to_event_coordination = OperatorPOWL(operator=Operator.XOR, children=[retail_delivery, event_coordination])
event_coordination_to_feedback_review = OperatorPOWL(operator=Operator.XOR, children=[event_coordination, feedback_review])

root = StrictPartialOrder(nodes=[milk_sourcing_to_diet_monitoring, diet_monitoring_to_culture_selection, culture_selection_to_milk_pasteurize, milk_pasteurize_to_curd_cutting, curd_cutting_to_whey_draining, whey_draining_to_mold_inoculate, mold_inoculate_to_press_forming, press_forming_to_salt_application, salt_application_to_aging_setup, aging_setup_to_humidity_control, humidity_control_to_flavor_testing, flavor_testing_to_packaging_design, packaging_design_to_order_processing, order_processing_to_retail_delivery, retail_delivery_to_event_coordination, event_coordination_to_feedback_review])
root.order.add_edge(milk_sourcing_to_diet_monitoring, diet_monitoring_to_culture_selection)
root.order.add_edge(diet_monitoring_to_culture_selection, culture_selection_to_milk_pasteurize)
root.order.add_edge(culture_selection_to_milk_pasteurize, milk_pasteurize_to_curd_cutting)
root.order.add_edge(milk_pasteurize_to_curd_cutting, curd_cutting_to_whey_draining)
root.order.add_edge(curd_cutting_to_whey_draining, whey_draining_to_mold_inoculate)
root.order.add_edge(whey_draining_to_mold_inoculate, mold_inoculate_to_press_forming)
root.order.add_edge(mold_inoculate_to_press_forming, press_forming_to_salt_application)
root.order.add_edge(press_forming_to_salt_application, salt_application_to_aging_setup)
