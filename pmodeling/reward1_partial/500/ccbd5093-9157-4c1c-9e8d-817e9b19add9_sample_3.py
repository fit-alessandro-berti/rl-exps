import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

scan_markets = Transition(label='Scan Markets')
host_workshops = Transition(label='Host Workshops')
form_teams = Transition(label='Form Teams')
develop_prototypes = Transition(label='Develop Prototypes')
simulate_tests = Transition(label='Simulate Tests')
collect_feedback = Transition(label='Collect Feedback')
review_ethics = Transition(label='Review Ethics')
conduct_analysis = Transition(label='Conduct Analysis')
identify_partners = Transition(label='Identify Partners')
align_strategy = Transition(label='Align Strategy')
launch_pilots = Transition(label='Launch Pilots')
monitor_trends = Transition(label='Monitor Trends')
ai_analytics = Transition(label='AI Analytics')
pivot_plans = Transition(label='Pivot Plans')
cycle_renewal = Transition(label='Cycle Renewal')

skip = SilentTransition()

xor1 = OperatorPOWL(operator=Operator.XOR, children=[scan_markets, host_workshops])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[form_teams, skip])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[develop_prototypes, simulate_tests])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[collect_feedback, review_ethics])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[conduct_analysis, identify_partners])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[align_strategy, launch_pilots])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[monitor_trends, ai_analytics])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[pivot_plans, cycle_renewal])

root = StrictPartialOrder(nodes=[xor1, xor2, xor3, xor4, xor5, xor6, xor7, xor8])
root.order.add_edge(xor1, xor2)
root.order.add_edge(xor1, xor3)
root.order.add_edge(xor1, xor4)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor2, xor5)
root.order.add_edge(xor3, xor6)
root.order.add_edge(xor3, xor7)
root.order.add_edge(xor4, xor6)
root.order.add_edge(xor4, xor7)
root.order.add_edge(xor5, xor8)
root.order.add_edge(xor6, xor8)