# Define the process
xor1 = OperatorPOWL(operator=Operator.XOR, children=[source_alternatives, supplier_audit])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[contract_review, regulation_check])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[inventory_scan, logistics_reroute])
