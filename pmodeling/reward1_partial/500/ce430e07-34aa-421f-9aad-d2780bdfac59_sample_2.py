xor8 = OperatorPOWL(operator=Operator.XOR, children=[report_generate, market_monitor])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[team_sync, risk_assess])

root = StrictPartialOrder(nodes=[xor1, xor2, xor3, xor4, xor5, xor6, xor7, xor8, xor9])
root.order.add_edge(xor1, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, xor5)
root.order.add_edge(xor5, xor6)
