import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
impact_study = Transition(label='Impact Study')
structure_check = Transition(label='Structure Check')
soil_testing = Transition(label='Soil Testing')
system_design = Transition(label='System Design')
seed_selection = Transition(label='Seed Selection')
irrigation_setup = Transition(label='Irrigation Setup')
lighting_install = Transition(label='Lighting Install')
pest_control = Transition(label='Pest Control')
community_meet = Transition(label='Community Meet')
regulation_review = Transition(label='Regulation Review')
waste_plan = Transition(label='Waste Plan')
crop_monitor = Transition(label='Crop Monitor')
harvest_prep = Transition(label='Harvest Prep')
market_launch = Transition(label='Market Launch')

skip = SilentTransition()

site_survey_to_impact_study = OperatorPOWL(operator=Operator.XOR, children=[impact_study, skip])
impact_study_to_structure_check = OperatorPOWL(operator=Operator.XOR, children=[structure_check, skip])
structure_check_to_soil_testing = OperatorPOWL(operator=Operator.XOR, children=[soil_testing, skip])
soil_testing_to_system_design = OperatorPOWL(operator=Operator.XOR, children=[system_design, skip])
system_design_to_seed_selection = OperatorPOWL(operator=Operator.XOR, children=[seed_selection, skip])
seed_selection_to_irrigation_setup = OperatorPOWL(operator=Operator.XOR, children=[irrigation_setup, skip])
irrigation_setup_to_lighting_install = OperatorPOWL(operator=Operator.XOR, children=[lighting_install, skip])
lighting_install_to_pest_control = OperatorPOWL(operator=Operator.XOR, children=[pest_control, skip])
pest_control_to_community_meet = OperatorPOWL(operator=Operator.XOR, children=[community_meet, skip])
community_meet_to_regulation_review = OperatorPOWL(operator=Operator.XOR, children=[regulation_review, skip])
regulation_review_to_waste_plan = OperatorPOWL(operator=Operator.XOR, children=[waste_plan, skip])
waste_plan_to_crop_monitor = OperatorPOWL(operator=Operator.XOR, children=[crop_monitor, skip])
crop_monitor_to_harvest_prep = OperatorPOWL(operator=Operator.XOR, children=[harvest_prep, skip])
harvest_prep_to_market_launch = OperatorPOWL(operator=Operator.XOR, children=[market_launch, skip])

root = StrictPartialOrder(nodes=[site_survey, impact_study, structure_check, soil_testing, system_design, seed_selection, irrigation_setup, lighting_install, pest_control, community_meet, regulation_review, waste_plan, crop_monitor, harvest_prep, market_launch])
root.order.add_edge(site_survey, impact_study)
root.order.add_edge(impact_study, structure_check)
root.order.add_edge(structure_check, soil_testing)
root.order.add_edge(soil_testing, system_design)
root.order.add_edge(system_design, seed_selection)
root.order.add_edge(seed_selection, irrigation_setup)
root.order.add_edge(irrigation_setup, lighting_install)
root.order.add_edge(lighting_install, pest_control)
root.order.add_edge(pest_control, community_meet)
root.order.add_edge(community_meet, regulation_review)
root.order.add_edge(regulation_review, waste_plan)
root.order.add_edge(waste_plan, crop_monitor)
root.order.add_edge(crop_monitor, harvest_prep)
root.order.add_edge(harvest_prep, market_launch)