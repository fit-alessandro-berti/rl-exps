import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

seed_sourcing = Transition(label='Seed Sourcing')
farm_scheduling = Transition(label='Farm Scheduling')
sensor_monitoring = Transition(label='Sensor Monitoring')
nutrient_cycling = Transition(label='Nutrient Cycling')
crop_forecasting = Transition(label='Crop Forecasting')
pest_inspection = Transition(label='Pest Inspection')
harvest_timing = Transition(label='Harvest Timing')
quality_check = Transition(label='Quality Check')
eco_packaging = Transition(label='Eco Packaging')
storage_allocation = Transition(label='Storage Allocation')
order_processing = Transition(label='Order Processing')
route_planning = Transition(label='Route Planning')
vehicle_dispatch = Transition(label='Vehicle Dispatch')
customer_feedback = Transition(label='Customer Feedback')
demand_analysis = Transition(label='Demand Analysis')
waste_management = Transition(label='Waste Management')
community_outreach = Transition(label='Community Outreach')

skip = SilentTransition()
xor1 = OperatorPOWL(operator=Operator.XOR, children=[pest_inspection, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[quality_check, skip])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[eco_packaging, skip])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[storage_allocation, skip])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[order_processing, skip])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[route_planning, skip])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[vehicle_dispatch, skip])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[customer_feedback, skip])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[demand_analysis, skip])
xor10 = OperatorPOWL(operator=Operator.XOR, children=[waste_management, skip])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[community_outreach, skip])

root = StrictPartialOrder(nodes=[seed_sourcing, farm_scheduling, sensor_monitoring, nutrient_cycling, crop_forecasting, xor1, xor2, xor3, xor4, xor5, xor6, xor7, xor8, xor9, xor10, xor11])
root.order.add_edge(seed_sourcing, farm_scheduling)
root.order.add_edge(farm_scheduling, sensor_monitoring)
root.order.add_edge(sensor_monitoring, nutrient_cycling)
root.order.add_edge(nutrient_cycling, crop_forecasting)
root.order.add_edge(crop_forecasting, xor1)
root.order.add_edge(crop_forecasting, xor2)
root.order.add_edge(crop_forecasting, xor3)
root.order.add_edge(crop_forecasting, xor4)
root.order.add_edge(crop_forecasting, xor5)
root.order.add_edge(crop_forecasting, xor6)
root.order.add_edge(crop_forecasting, xor7)
root.order.add_edge(crop_forecasting, xor8)
root.order.add_edge(crop_forecasting, xor9)
root.order.add_edge(crop_forecasting, xor10)
root.order.add_edge(crop_forecasting, xor11)