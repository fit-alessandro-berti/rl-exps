import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

# Define the activities
provenance_check = Transition(label='Provenance Check')
material_scan = Transition(label='Material Scan')
radiocarbon_test = Transition(label='Radiocarbon Test')
style_compare = Transition(label='Style Compare')
database_query = Transition(label='Database Query')
blockchain_prep = Transition(label='Blockchain Prep')
legal_review = Transition(label='Legal Review')
ownership_audit = Transition(label='Ownership Audit')
conservation_plan = Transition(label='Conservation Plan')
expert_panel = Transition(label='Expert Panel')
report_draft = Transition(label='Report Draft')
client_review = Transition(label='Client Review')
authority_submit = Transition(label='Authority Submit')
exhibit_setup = Transition(label='Exhibit Setup')
final_approval = Transition(label='Final Approval')

# Define the relationships
provenance_check_next = OperatorPOWL(operator=Operator.FORK, children=[material_scan, radiocarbon_test])
material_scan_next = OperatorPOWL(operator=Operator.XOR, children=[style_compare, database_query])
radiocarbon_test_next = OperatorPOWL(operator=Operator.XOR, children=[style_compare, database_query])
style_compare_next = OperatorPOWL(operator=Operator.XOR, children=[blockchain_prep, legal_review])
database_query_next = OperatorPOWL(operator=Operator.XOR, children=[blockchain_prep, legal_review])
blockchain_prep_next = OperatorPOWL(operator=Operator.FORK, children=[ownership_audit, conservation_plan])
legal_review_next = OperatorPOWL(operator=Operator.FORK, children=[ownership_audit, conservation_plan])
ownership_audit_next = OperatorPOWL(operator=Operator.XOR, children=[report_draft, exhibit_setup])
conservation_plan_next = OperatorPOWL(operator=Operator.XOR, children=[report_draft, exhibit_setup])
report_draft_next = OperatorPOWL(operator=Operator.FORK, children=[client_review, authority_submit])
client_review_next = OperatorPOWL(operator=Operator.FORK, children=[final_approval, exhibit_setup])
authority_submit_next = OperatorPOWL(operator=Operator.FORK, children=[final_approval, exhibit_setup])
exhibit_setup_next = OperatorPOWL(operator=Operator.FORK, children=[final_approval, exhibit_setup])
final_approval_next = OperatorPOWL(operator=Operator.FORK, children=[final_approval, exhibit_setup])

# Define the root
root = StrictPartialOrder(nodes=[provenance_check, material_scan, radiocarbon_test, style_compare, database_query, blockchain_prep, legal_review, ownership_audit, conservation_plan, expert_panel, report_draft, client_review, authority_submit, exhibit_setup, final_approval])
root.order.add_edge(provenance_check, provenance_check_next)
root.order.add_edge(provenance_check_next, material_scan)
root.order.add_edge(provenance_check_next, radiocarbon_test)
root.order.add_edge(material_scan, style_compare)
root.order.add_edge(material_scan, database_query)
root.order.add_edge(radiocarbon_test, style_compare)
root.order.add_edge(radiocarbon_test, database_query)
root.order.add_edge(style_compare, blockchain_prep)
root.order.add_edge(style_compare, legal_review)
root.order.add_edge(database_query, blockchain_prep)
root.order.add_edge(database_query, legal_review)
root.order.add_edge(blockchain_prep, ownership_audit)
root.order.add_edge(blockchain_prep, conservation_plan)
root.order.add_edge(legal_review, ownership_audit)
root.order.add_edge(legal_review, conservation_plan)
root.order.add_edge(ownership_audit, report_draft)
root.order.add_edge(ownership_audit, exhibit_setup)
root.order.add_edge(conservation_plan, report_draft)
root.order.add_edge(conservation_plan, exhibit_setup)
root.order.add_edge(report_draft, client_review)
root.order.add_edge(report_draft, authority_submit)
root.order.add_edge(client_review, final_approval)
root.order.add_edge(client_review, exhibit_setup)
root.order.add_edge(authority_submit, final_approval)
root.order.add_edge(authority_submit, exhibit_setup)
root.order.add_edge(exhibit_setup, final_approval)
root.order.add_edge(exhibit_setup, final_approval)