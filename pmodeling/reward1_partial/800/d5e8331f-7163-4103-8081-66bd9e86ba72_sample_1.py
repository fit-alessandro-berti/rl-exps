import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition

# Define the activities
site_prep = Transition(label='Site Prep')
seed_select = Transition(label='Seed Select')
nutrient_mix = Transition(label='Nutrient Mix')
planting_rows = Transition(label='Planting Rows')
env_monitor = Transition(label='Env Monitor')
water_adjust = Transition(label='Water Adjust')
pest_control = Transition(label='Pest Control')
growth_check = Transition(label='Growth Check')
light_calibrate = Transition(label='Light Calibrate')
energy_manage = Transition(label='Energy Manage')
harvest_crop = Transition(label='Harvest Crop')
quality_sort = Transition(label='Quality Sort')
pack_goods = Transition(label='Pack Goods')
cold_store = Transition(label='Cold Store')
market_ship = Transition(label='Market Ship')
data_analyze = Transition(label='Data Analyze')

# Define the control-flow operators
xor1 = OperatorPOWL(operator=Operator.XOR, children=[nutrient_mix, planting_rows])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[env_monitor, water_adjust])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[pest_control, growth_check])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[light_calibrate, energy_manage])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[harvest_crop, quality_sort])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[pack_goods, cold_store])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[market_ship, data_analyze])

# Define the partial order
root = StrictPartialOrder(nodes=[site_prep, seed_select, xor1, xor2, xor3, xor4, xor5, xor6, xor7])
root.order.add_edge(site_prep, seed_select)
root.order.add_edge(seed_select, xor1)
root.order.add_edge(xor1, nutrient_mix)
root.order.add_edge(xor1, planting_rows)
root.order.add_edge(nutrient_mix, xor2)
root.order.add_edge(planting_rows, xor2)
root.order.add_edge(xor2, env_monitor)
root.order.add_edge(xor2, water_adjust)
root.order.add_edge(env_monitor, xor3)
root.order.add_edge(water_adjust, xor3)
root.order.add_edge(xor3, pest_control)
root.order.add_edge(xor3, growth_check)
root.order.add_edge(pest_control, xor4)
root.order.add_edge(growth_check, xor4)
root.order.add_edge(xor4, light_calibrate)
root.order.add_edge(xor4, energy_manage)
root.order.add_edge(light_calibrate, xor5)
root.order.add_edge(energy_manage, xor5)
root.order.add_edge(xor5, harvest_crop)
root.order.add_edge(xor5, quality_sort)
root.order.add_edge(harvest_crop, xor6)
root.order.add_edge(quality_sort, xor6)
root.order.add_edge(xor6, pack_goods)
root.order.add_edge(xor6, cold_store)
root.order.add_edge(pack_goods, xor7)
root.order.add_edge(cold_store, xor7)
root.order.add_edge(xor7, market_ship)
root.order.add_edge(xor7, data_analyze)

# Print the root
print(root)