import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

provenance_check = Transition(label='Provenance Check')
sample_collection = Transition(label='Sample Collection')
spectroscopy_test = Transition(label='Spectroscopy Test')
carbon_dating = Transition(label='Carbon Dating')
expert_review = Transition(label='Expert Review')
legal_clearance = Transition(label='Legal Clearance')
cultural_assessment = Transition(label='Cultural Assessment')
digital_scan = Transition(label='Digital Scan')
report_draft = Transition(label='Report Draft')
stakeholder_meet = Transition(label='Stakeholder Meet')
acquisition_vote = Transition(label='Acquisition Vote')
restoration_plan = Transition(label='Restoration Plan')
condition_report = Transition(label='Condition Report')
archival_entry = Transition(label='Archival Entry')
final_approval = Transition(label='Final Approval')

skip = SilentTransition()

provenance_check_next = OperatorPOWL(operator=Operator.XOR, children=[provenance_check, sample_collection])
sample_collection_next = OperatorPOWL(operator=Operator.XOR, children=[spectroscopy_test, carbon_dating])
spectroscopy_test_next = OperatorPOWL(operator=Operator.XOR, children=[expert_review, legal_clearance])
carbon_dating_next = OperatorPOWL(operator=Operator.XOR, children=[cultural_assessment, digital_scan])
expert_review_next = OperatorPOWL(operator=Operator.XOR, children=[report_draft, stakeholder_meet])
legal_clearance_next = OperatorPOWL(operator=Operator.XOR, children=[acquisition_vote, restoration_plan])
cultural_assessment_next = OperatorPOWL(operator=Operator.XOR, children=[condition_report, archival_entry])
digital_scan_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])
report_draft_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])
stakeholder_meet_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])
acquisition_vote_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])
restoration_plan_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])
condition_report_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])
archival_entry_next = OperatorPOWL(operator=Operator.XOR, children=[final_approval, skip])

root = StrictPartialOrder(nodes=[provenance_check_next, sample_collection_next, spectroscopy_test_next, carbon_dating_next, expert_review_next, legal_clearance_next, cultural_assessment_next, digital_scan_next, report_draft_next, stakeholder_meet_next, acquisition_vote_next, restoration_plan_next, condition_report_next, archival_entry_next])
root.order.add_edge(provenance_check_next, sample_collection_next)
root.order.add_edge(sample_collection_next, spectroscopy_test_next)
root.order.add_edge(spectroscopy_test_next, expert_review_next)
root.order.add_edge(expert_review_next, report_draft_next)
root.order.add_edge(report_draft_next, stakeholder_meet_next)
root.order.add_edge(stakeholder_meet_next, acquisition_vote_next)
root.order.add_edge(acquisition_vote_next, restoration_plan_next)
root.order.add_edge(restoration_plan_next, condition_report_next)
root.order.add_edge(condition_report_next, archival_entry_next)
root.order.add_edge(archival_entry_next, final_approval_next)