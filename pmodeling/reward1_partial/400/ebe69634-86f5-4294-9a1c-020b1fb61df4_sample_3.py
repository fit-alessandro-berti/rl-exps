import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

client_intake = Transition(label='Client Intake')
needs_analysis = Transition(label='Needs Analysis')
developer_match = Transition(label='Developer Match')
expert_vetting = Transition(label='Expert Vetting')
prototype_build = Transition(label='Prototype Build')
feedback_loop = Transition(label='Feedback Loop')
model_refinement = Transition(label='Model Refinement')
license_draft = Transition(label='License Draft')
ip_negotiation = Transition(label='IP Negotiation')
contract_sign = Transition(label='Contract Sign')
deployment_prep = Transition(label='Deployment Prep')
go_live = Transition(label='Go Live')
monitor_model = Transition(label='Monitor Model')
optimize_ai = Transition(label='Optimize AI')
support_handoff = Transition(label='Support Handoff')
compliance_check = Transition(label='Compliance Check')
final_review = Transition(label='Final Review')

skip = SilentTransition()
xor = OperatorPOWL(operator=Operator.XOR, children=[skip, expert_vetting])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[prototype_build, feedback_loop])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[model_refinement, xor])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[license_draft, ip_negotiation])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[contract_sign, xor4])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[deployment_prep, xor5])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[go_live, xor6])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[monitor_model, xor7])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[optimize_ai, xor8])
xor10 = OperatorPOWL(operator=Operator.XOR, children=[support_handoff, xor9])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[compliance_check, xor10])
xor12 = OperatorPOWL(operator=Operator.XOR, children=[final_review, xor11])

root = StrictPartialOrder(nodes=[client_intake, needs_analysis, developer_match, xor, xor2, xor3, xor4, xor5, xor6, xor7, xor8, xor9, xor10, xor11, xor12])
root.order.add_edge(client_intake, needs_analysis)
root.order.add_edge(needs_analysis, developer_match)
root.order.add_edge(developer_match, xor)
root.order.add_edge(xor, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, xor5)
root.order.add_edge(xor5, xor6)
root.order.add_edge(xor6, xor7)
root.order.add_edge(xor7, xor8)
root.order.add_edge(xor8, xor9)
root.order.add_edge(xor9, xor10)
root.order.add_edge(xor10, xor11)
root.order.add_edge(xor11, xor12)