import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

provenance_check = Transition(label='Provenance Check')
specimen_sampling = Transition(label='Specimen Sampling')
spectroscopy_test = Transition(label='Spectroscopy Test')
radiocarbon_date = Transition(label='Radiocarbon Date')
material_analysis = Transition(label='Material Analysis')
forensic_review = Transition(label='Forensic Review')
expert_consult = Transition(label='Expert Consult')
legal_verify = Transition(label='Legal Verify')
ownership_audit = Transition(label='Ownership Audit')
risk_assess = Transition(label='Risk Assess')
insurance_quote = Transition(label='Insurance Quote')
condition_report = Transition(label='Condition Report')
documentation = Transition(label='Documentation')
committee_review = Transition(label='Committee Review')
final_approval = Transition(label='Final Approval')

skip = SilentTransition()
xor1 = OperatorPOWL(operator=Operator.XOR, children=[ownership_audit, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[risk_assess, insurance_quote])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[condition_report, documentation])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[committee_review, final_approval])
root = StrictPartialOrder(nodes=[provenance_check, specimen_sampling, spectroscopy_test, radiocarbon_date, material_analysis, forensic_review, expert_consult, legal_verify, xor1, xor2, xor3, xor4])
root.order.add_edge(provenance_check, specimen_sampling)
root.order.add_edge(specimen_sampling, spectroscopy_test)
root.order.add_edge(spectroscopy_test, radiocarbon_date)
root.order.add_edge(radiocarbon_date, material_analysis)
root.order.add_edge(material_analysis, forensic_review)
root.order.add_edge(forensic_review, expert_consult)
root.order.add_edge(expert_consult, legal_verify)
root.order.add_edge(legal_verify, xor1)
root.order.add_edge(xor1, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, final_approval)