from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

# Define the transitions
farm_selection = Transition(label='Farm Selection')
milk_testing = Transition(label='Milk Testing')
batch_pastureize = Transition(label='Batch Pasteurize')
culture_add = Transition(label='Culture Add')
curd_cut = Transition(label='Curd Cut')
whey_drain = Transition(label='Whey Drain')
mold_inoculate = Transition(label='Mold Inoculate')
press_form = Transition(label='Press Form')
salt_rub = Transition(label='Salt Rub')
aging_monitor = Transition(label='Aging Monitor')
flavor_adjust = Transition(label='Flavor Adjust')
packaging_design = Transition(label='Packaging Design')
label_approval = Transition(label='Label Approval')
order_processing = Transition(label='Order Processing')
cold_storage = Transition(label='Cold Storage')
delivery_schedule = Transition(label='Delivery Schedule')
retail_setup = Transition(label='Retail Setup')
feedback_collect = Transition(label='Feedback Collect')

# Define the loop and choice nodes
loop = OperatorPOWL(operator=Operator.LOOP, children=[aging_monitor, flavor_adjust])
choice = OperatorPOWL(operator=Operator.XOR, children=[packaging_design, label_approval])
choice_2 = OperatorPOWL(operator=Operator.XOR, children=[order_processing, cold_storage])
choice_3 = OperatorPOWL(operator=Operator.XOR, children=[delivery_schedule, retail_setup])
choice_4 = OperatorPOWL(operator=Operator.XOR, children=[feedback_collect, choice_3])

# Define the root node
root = StrictPartialOrder(nodes=[farm_selection, milk_testing, batch_pastureize, culture_add, curd_cut, whey_drain, mold_inoculate, press_form, salt_rub, loop, choice, choice_2, choice_3, choice_4])
root.order.add_edge(farm_selection, milk_testing)
root.order.add_edge(milk_testing, batch_pastureize)
root.order.add_edge(batch_pastureize, culture_add)
root.order.add_edge(culture_add, curd_cut)
root.order.add_edge(curd_cut, whey_drain)
root.order.add_edge(whey_drain, mold_inoculate)
root.order.add_edge(mold_inoculate, press_form)
root.order.add_edge(press_form, salt_rub)
root.order.add_edge(salt_rub, loop)
root.order.add_edge(loop, choice)
root.order.add_edge(choice, choice_2)
root.order.add_edge(choice_2, choice_3)
root.order.add_edge(choice_3, choice_4)
root.order.add_edge(choice_4, choice)