from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

# Define the activities
site_survey = Transition(label='Site Survey')
design_layout = Transition(label='Design Layout')
module_build = Transition(label='Module Build')
system_install = Transition(label='System Install')
water_prep = Transition(label='Water Prep')
seed_selection = Transition(label='Seed Selection')
nutrient_mix = Transition(label='Nutrient Mix')
climate_setup = Transition(label='Climate Setup')
sensor_deploy = Transition(label='Sensor Deploy')
pest_scan = Transition(label='Pest Scan')
growth_monitor = Transition(label='Growth Monitor')
data_sync = Transition(label='Data Sync')
energy_manage = Transition(label='Energy Manage')
harvest_plan = Transition(label='Harvest Plan')
community_link = Transition(label='Community Link')

# Define the relationships between activities
site_survey_next = OperatorPOWL(operator=Operator.NEXT, children=[design_layout])
design_layout_next = OperatorPOWL(operator=Operator.NEXT, children=[module_build])
module_build_next = OperatorPOWL(operator=Operator.NEXT, children=[system_install])
system_install_next = OperatorPOWL(operator=Operator.NEXT, children=[water_prep])
water_prep_next = OperatorPOWL(operator=Operator.NEXT, children=[seed_selection])
seed_selection_next = OperatorPOWL(operator=Operator.NEXT, children=[nutrient_mix])
nutrient_mix_next = OperatorPOWL(operator=Operator.NEXT, children=[climate_setup])
climate_setup_next = OperatorPOWL(operator=Operator.NEXT, children=[sensor_deploy])
sensor_deploy_next = OperatorPOWL(operator=Operator.NEXT, children=[pest_scan])
pest_scan_next = OperatorPOWL(operator=Operator.NEXT, children=[growth_monitor])
growth_monitor_next = OperatorPOWL(operator=Operator.NEXT, children=[data_sync])
data_sync_next = OperatorPOWL(operator=Operator.NEXT, children=[energy_manage])
energy_manage_next = OperatorPOWL(operator=Operator.NEXT, children=[harvest_plan])
harvest_plan_next = OperatorPOWL(operator=Operator.NEXT, children=[community_link])

# Define the root POWL model
root = StrictPartialOrder(nodes=[site_survey, design_layout, module_build, system_install, water_prep, seed_selection, nutrient_mix, climate_setup, sensor_deploy, pest_scan, growth_monitor, data_sync, energy_manage, harvest_plan, community_link])
root.order.add_edge(site_survey, design_layout_next)
root.order.add_edge(design_layout, module_build_next)
root.order.add_edge(module_build, system_install_next)
root.order.add_edge(system_install, water_prep_next)
root.order.add_edge(water_prep, seed_selection_next)
root.order.add_edge(seed_selection, nutrient_mix_next)
root.order.add_edge(nutrient_mix, climate_setup_next)
root.order.add_edge(climate_setup, sensor_deploy_next)
root.order.add_edge(sensor_deploy, pest_scan_next)
root.order.add_edge(pest_scan, growth_monitor_next)
root.order.add_edge(growth_monitor, data_sync_next)
root.order.add_edge(data_sync, energy_manage_next)
root.order.add_edge(energy_manage, harvest_plan_next)
root.order.add_edge(harvest_plan, community_link_next)