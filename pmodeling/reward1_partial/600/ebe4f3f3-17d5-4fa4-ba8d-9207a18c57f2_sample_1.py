import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

milk_sourcing = Transition(label='Milk Sourcing')
farm_audit = Transition(label='Farm Audit')
milk_testing = Transition(label='Milk Testing')
batch_forming = Transition(label='Batch Forming')
curd_cutting = Transition(label='Curd Cutting')
molding_cheese = Transition(label='Molding Cheese')
salting_process = Transition(label='Salting Process')
aging_control = Transition(label='Aging Control')
quality_check = Transition(label='Quality Check')
packaging_design = Transition(label='Packaging Design')
label_printing = Transition(label='Label Printing')
inventory_update = Transition(label='Inventory Update')
order_receiving = Transition(label='Order Receiving')
retail_coordination = Transition(label='Retail Coordination')
shipping_prep = Transition(label='Shipping Prep')
customer_feedback = Transition(label='Customer Feedback')
demand_forecast = Transition(label='Demand Forecast')
limited_release = Transition(label='Limited Release')

loop_aging = OperatorPOWL(operator=Operator.LOOP, children=[aging_control])
loop_packaging = OperatorPOWL(operator=Operator.LOOP, children=[packaging_design, label_printing])
loop_order = OperatorPOWL(operator=Operator.LOOP, children=[order_receiving, retail_coordination, shipping_prep])
xor_feedback = OperatorPOWL(operator=Operator.XOR, children=[customer_feedback, limited_release])

root = StrictPartialOrder(nodes=[milk_sourcing, farm_audit, milk_testing, batch_forming, curd_cutting, molding_cheese, salting_process, loop_aging, quality_check, loop_packaging, inventory_update, order_receiving, retail_coordination, shipping_prep, xor_feedback, customer_feedback, demand_forecast, limited_release])
root.order.add_edge(milk_sourcing, farm_audit)
root.order.add_edge(farm_audit, milk_testing)
root.order.add_edge(milk_testing, batch_forming)
root.order.add_edge(batch_forming, curd_cutting)
root.order.add_edge(curd_cutting, molding_cheese)
root.order.add_edge(molding_cheese, salting_process)
root.order.add_edge(salting_process, loop_aging)
root.order.add_edge(loop_aging, quality_check)
root.order.add_edge(quality_check, loop_packaging)
root.order.add_edge(loop_packaging, inventory_update)
root.order.add_edge(inventory_update, order_receiving)
root.order.add_edge(order_receiving, retail_coordination)
root.order.add_edge(retail_coordination, shipping_prep)
root.order.add_edge(shipping_prep, xor_feedback)
root.order.add_edge(xor_feedback, customer_feedback)
root.order.add_edge(customer_feedback, demand_forecast)
root.order.add_edge(demand_forecast, limited_release)

print(root)