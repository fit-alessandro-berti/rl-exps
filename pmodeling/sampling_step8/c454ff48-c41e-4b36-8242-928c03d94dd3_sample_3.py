from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_assess = Transition(label='Site Assess')
permit_obtain = Transition(label='Permit Obtain')
soil_testing = Transition(label='Soil Testing')
crop_select = Transition(label='Crop Select')
irrigation_setup = Transition(label='Irrigation Setup')
drainage_install = Transition(label='Drainage Install')
energy_integrate = Transition(label='Energy Integrate')
staff_train = Transition(label='Staff Train')
pest_control = Transition(label='Pest Control')
logistics_plan = Transition(label='Logistics Plan')
supply_coordinate = Transition(label='Supply Coordinate')
distribution_map = Transition(label='Distribution Map')
community_engage = Transition(label='Community Engage')
monitoring_setup = Transition(label='Monitoring Setup')
yield_optimize = Transition(label='Yield Optimize')

skip = SilentTransition()

site_assess_permits = OperatorPOWL(operator=Operator.XOR, children=[site_assess, permit_obtain])
soil_testing_crop_select = OperatorPOWL(operator=Operator.XOR, children=[soil_testing, crop_select])
irrigation_setup_drainage_install = OperatorPOWL(operator=Operator.XOR, children=[irrigation_setup, drainage_install])
energy_integrate_staff_train = OperatorPOWL(operator=Operator.XOR, children=[energy_integrate, staff_train])
pest_control_logistics_plan = OperatorPOWL(operator=Operator.XOR, children=[pest_control, logistics_plan])
supply_coordinate_distribution_map = OperatorPOWL(operator=Operator.XOR, children=[supply_coordinate, distribution_map])
community_engage_monitoring_setup = OperatorPOWL(operator=Operator.XOR, children=[community_engage, monitoring_setup])
yield_optimize = Transition(label='Yield Optimize')

root = StrictPartialOrder(nodes=[site_assess_permits, soil_testing_crop_select, irrigation_setup_drainage_install, energy_integrate_staff_train, pest_control_logistics_plan, supply_coordinate_distribution_map, community_engage_monitoring_setup, yield_optimize])
root.order.add_edge(site_assess_permits, soil_testing_crop_select)
root.order.add_edge(soil_testing_crop_select, irrigation_setup_drainage_install)
root.order.add_edge(irrigation_setup_drainage_install, energy_integrate_staff_train)
root.order.add_edge(energy_integrate_staff_train, pest_control_logistics_plan)
root.order.add_edge(pest_control_logistics_plan, supply_coordinate_distribution_map)
root.order.add_edge(supply_coordinate_distribution_map, community_engage_monitoring_setup)
root.order.add_edge(community_engage_monitoring_setup, yield_optimize)