import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

seed_selection = Transition(label='Seed Selection')
nutrient_setup = Transition(label='Nutrient Setup')
growth_monitoring = Transition(label='Growth Monitoring')
climate_adjust = Transition(label='Climate Adjust')
pest_control = Transition(label='Pest Control')
water_recirculate = Transition(label='Water Recirculate')
light_calibration = Transition(label='Light Calibration')
robotic_harvest = Transition(label='Robotic Harvest')
quality_inspect = Transition(label='Quality Inspect')
waste_process = Transition(label='Waste Process')
energy_reuse = Transition(label='Energy Reuse')
inventory_update = Transition(label='Inventory Update')
demand_forecast = Transition(label='Demand Forecast')
order_dispatch = Transition(label='Order Dispatch')
community_event = Transition(label='Community Event')
feedback_collect = Transition(label='Feedback Collect')
data_analyze = Transition(label='Data Analyze')

skip = SilentTransition()

loop1 = OperatorPOWL(operator=Operator.LOOP, children=[climate_adjust, pest_control])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[water_recirculate, light_calibration])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[inventory_update, demand_forecast])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[order_dispatch, community_event])
loop5 = OperatorPOWL(operator=Operator.LOOP, children=[feedback_collect, data_analyze])

xor1 = OperatorPOWL(operator=Operator.XOR, children=[quality_inspect, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[waste_process, energy_reuse])

root = StrictPartialOrder(nodes=[seed_selection, nutrient_setup, growth_monitoring, loop1, loop2, loop3, loop4, loop5, xor1, xor2])
root.order.add_edge(seed_selection, nutrient_setup)
root.order.add_edge(nutrient_setup, growth_monitoring)
root.order.add_edge(growth_monitoring, loop1)
root.order.add_edge(loop1, climate_adjust)
root.order.add_edge(loop1, pest_control)
root.order.add_edge(pest_control, loop1)
root.order.add_edge(climate_adjust, loop1)
root.order.add_edge(loop2, water_recirculate)
root.order.add_edge(loop2, light_calibration)
root.order.add_edge(light_calibration, loop2)
root.order.add_edge(water_recirculate, loop2)
root.order.add_edge(loop3, inventory_update)
root.order.add_edge(loop3, demand_forecast)
root.order.add_edge(demand_forecast, loop3)
root.order.add_edge(inventory_update, loop3)
root.order.add_edge(loop4, order_dispatch)
root.order.add_edge(loop4, community_event)
root.order.add_edge(community_event, loop4)
root.order.add_edge(order_dispatch, loop4)
root.order.add_edge(loop5, feedback_collect)
root.order.add_edge(loop5, data_analyze)
root.order.add_edge(data_analyze, loop5)
root.order.add_edge(feedback_collect, loop5)
root.order.add_edge(xor1, quality_inspect)
root.order.add_edge(xor1, skip)
root.order.add_edge(xor2, waste_process)
root.order.add_edge(xor2, energy_reuse)