import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
Site_Survey = Transition(label='Site Survey')
System_Design = Transition(label='System Design')
Module_Build = Transition(label='Module Build')
Nutrient_Mix = Transition(label='Nutrient Mix')
Seed_Selection = Transition(label='Seed Selection')
Planting_Plan = Transition(label='Planting Plan')
Irrigation_Setup = Transition(label='Irrigation Setup')
Climate_Control = Transition(label='Climate Control')
Lighting_Adjust = Transition(label='Lighting Adjust')
Pest_Monitor = Transition(label='Pest Monitor')
Waste_Cycle = Transition(label='Waste Cycle')
Data_Capture = Transition(label='Data Capture')
Yield_Forecast = Transition(label='Yield Forecast')
Regulation_Check = Transition(label='Regulation Check')
Community_Meet = Transition(label='Community Meet')
Harvest_Prep = Transition(label='Harvest Prep')
Market_Link = Transition(label='Market Link')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Site_Survey, System_Design, Module_Build, Nutrient_Mix, Seed_Selection, Planting_Plan, Irrigation_Setup, Climate_Control, Lighting_Adjust, Pest_Monitor, Waste_Cycle])
xor = OperatorPOWL(operator=Operator.XOR, children=[Data_Capture, Yield_Forecast, Regulation_Check, Community_Meet, Harvest_Prep, Market_Link])
root = StrictPartialOrder(nodes=[loop, xor])
root.order.add_edge(loop, xor)