import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
design_layout = Transition(label='Design Layout')
install_modules = Transition(label='Install Modules')
calibrate_climate = Transition(label='Calibrate Climate')
prepare_nutrients = Transition(label='Prepare Nutrients')
select_seeds = Transition(label='Select Seeds')
start_germination = Transition(label='Start Germination')
deploy_sensors = Transition(label='Deploy Sensors')
monitor_growth = Transition(label='Monitor Growth')
manage_pests = Transition(label='Manage Pests')
schedule_harvest = Transition(label='Schedule Harvest')
process_waste = Transition(label='Process Waste')
optimize_energy = Transition(label='Optimize Energy')
conduct_training = Transition(label='Conduct Training')
update_records = Transition(label='Update Records')
review_performance = Transition(label='Review Performance')
skip = SilentTransition()

site_survey_loop = OperatorPOWL(operator=Operator.LOOP, children=[site_survey, design_layout])
design_layout_loop = OperatorPOWL(operator=Operator.LOOP, children=[install_modules, calibrate_climate])
install_modules_loop = OperatorPOWL(operator=Operator.LOOP, children=[prepare_nutrients, select_seeds])
calibrate_climate_loop = OperatorPOWL(operator=Operator.LOOP, children=[start_germination, deploy_sensors])
prepare_nutrients_loop = OperatorPOWL(operator=Operator.LOOP, children=[monitor_growth, manage_pests])
select_seeds_loop = OperatorPOWL(operator=Operator.LOOP, children=[schedule_harvest, process_waste])
start_germination_loop = OperatorPOWL(operator=Operator.LOOP, children=[optimize_energy, conduct_training])
deploy_sensors_loop = OperatorPOWL(operator=Operator.LOOP, children=[update_records, review_performance])

root = StrictPartialOrder(nodes=[site_survey_loop, design_layout_loop, install_modules_loop, calibrate_climate_loop, prepare_nutrients_loop, select_seeds_loop, start_germination_loop, deploy_sensors_loop])
root.order.add_edge(site_survey_loop, design_layout_loop)
root.order.add_edge(design_layout_loop, install_modules_loop)
root.order.add_edge(install_modules_loop, calibrate_climate_loop)
root.order.add_edge(calibrate_climate_loop, prepare_nutrients_loop)
root.order.add_edge(prepare_nutrients_loop, select_seeds_loop)
root.order.add_edge(select_seeds_loop, start_germination_loop)
root.order.add_edge(start_germination_loop, deploy_sensors_loop)