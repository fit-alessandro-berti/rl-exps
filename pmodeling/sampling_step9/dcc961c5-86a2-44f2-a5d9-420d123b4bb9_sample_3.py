import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
SiteSurvey = Transition(label='Site Survey')
DesignLayout = Transition(label='Design Layout')
PermitAcquire = Transition(label='Permit Acquire')
ModularBuild = Transition(label='Modular Build')
ClimateSetup = Transition(label='Climate Setup')
NutrientMix = Transition(label='Nutrient Mix')
SeedAutomation = Transition(label='Seed Automation')
PestControl = Transition(label='Pest Control')
EnergyAudit = Transition(label='Energy Audit')
SensorInstall = Transition(label='Sensor Install')
GrowthMonitor = Transition(label='Growth Monitor')
WasteProcess = Transition(label='Waste Process')
DataAnalysis = Transition(label='Data Analysis')
StaffTrain = Transition(label='Staff Train')
CommunityLink = Transition(label='Community Link')
YieldReport = Transition(label='Yield Report')
skip = SilentTransition()
site_survey = OperatorPOWL(operator=Operator.XOR, children=[SiteSurvey, skip])
design_layout = OperatorPOWL(operator=Operator.XOR, children=[DesignLayout, skip])
permit_acquire = OperatorPOWL(operator=Operator.XOR, children=[PermitAcquire, skip])
modular_build = OperatorPOWL(operator=Operator.XOR, children=[ModularBuild, skip])
climate_setup = OperatorPOWL(operator=Operator.XOR, children=[ClimateSetup, skip])
nutrient_mix = OperatorPOWL(operator=Operator.XOR, children=[NutrientMix, skip])
seed_automation = OperatorPOWL(operator=Operator.XOR, children=[SeedAutomation, skip])
pest_control = OperatorPOWL(operator=Operator.XOR, children=[PestControl, skip])
energy_audit = OperatorPOWL(operator=Operator.XOR, children=[EnergyAudit, skip])
sensor_install = OperatorPOWL(operator=Operator.XOR, children=[SensorInstall, skip])
growth_monitor = OperatorPOWL(operator=Operator.XOR, children=[GrowthMonitor, skip])
waste_process = OperatorPOWL(operator=Operator.XOR, children=[WasteProcess, skip])
data_analysis = OperatorPOWL(operator=Operator.XOR, children=[DataAnalysis, skip])
staff_train = OperatorPOWL(operator=Operator.XOR, children=[StaffTrain, skip])
community_link = OperatorPOWL(operator=Operator.XOR, children=[CommunityLink, skip])
yield_report = OperatorPOWL(operator=Operator.XOR, children=[YieldReport, skip])
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[site_survey, design_layout])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[permit_acquire, modular_build])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[climate_setup, nutrient_mix])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[seed_automation, pest_control])
loop5 = OperatorPOWL(operator=Operator.LOOP, children=[energy_audit, sensor_install])
loop6 = OperatorPOWL(operator=Operator.LOOP, children=[growth_monitor, waste_process])
loop7 = OperatorPOWL(operator=Operator.LOOP, children=[data_analysis, staff_train])
loop8 = OperatorPOWL(operator=Operator.LOOP, children=[community_link, yield_report])
root = StrictPartialOrder(nodes=[loop1, loop2, loop3, loop4, loop5, loop6, loop7, loop8])
root.order.add_edge(loop1, loop2)
root.order.add_edge(loop2, loop3)
root.order.add_edge(loop3, loop4)
root.order.add_edge(loop4, loop5)
root.order.add_edge(loop5, loop6)
root.order.add_edge(loop6, loop7)
root.order.add_edge(loop7, loop8)