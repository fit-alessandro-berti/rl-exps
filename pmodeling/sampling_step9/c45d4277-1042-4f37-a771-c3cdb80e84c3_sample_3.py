import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
Trend_Scan = Transition(label='Trend Scan')
Opportunity_Map = Transition(label='Opportunity Map')
Expert_Gather = Transition(label='Expert Gather')
Idea_Sprint = Transition(label='Idea Sprint')
Proto_Build = Transition(label='Proto Build')
User_Feedback = Transition(label='User Feedback')
Risk_Review = Transition(label='Risk Review')
IP_Audit = Transition(label='IP Audit')
Pilot_Launch = Transition(label='Pilot Launch')
Stakeholder_Meet = Transition(label='Stakeholder Meet')
Resource_Shift = Transition(label='Resource Shift')
Scale_Up = Transition(label='Scale Up')
Impact_Assess = Transition(label='Impact Assess')
Knowledge_Share = Transition(label='Knowledge Share')
Monitor_Trends = Transition(label='Monitor Trends')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Idea_Sprint, Proto_Build])
xor = OperatorPOWL(operator=Operator.XOR, children=[Risk_Review, IP_Audit])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[Pilot_Launch, skip])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[Stakeholder_Meet, Resource_Shift])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[Scale_Up, skip])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[Impact_Assess, skip])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[Knowledge_Share, Monitor_Trends])
root = StrictPartialOrder(nodes=[Trend_Scan, Opportunity_Map, Expert_Gather, loop, xor, xor2, xor3, xor4, xor5, xor6])
root.order.add_edge(Trend_Scan, Opportunity_Map)
root.order.add_edge(Trend_Scan, Expert_Gather)
root.order.add_edge(Opportunity_Map, Expert_Gather)
root.order.add_edge(Expert_Gather, loop)
root.order.add_edge(Expert_Gather, xor)
root.order.add_edge(loop, xor2)
root.order.add_edge(xor, xor2)
root.order.add_edge(xor, xor3)
root.order.add_edge(xor2, xor4)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, xor5)
root.order.add_edge(xor5, xor6)
root.order.add_edge(xor6, Monitor_Trends)