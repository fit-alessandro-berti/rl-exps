import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

req_analysis = Transition(label='Requirement Analysis')
comp_sourcing = Transition(label='Component Sourcing')
quality_check = Transition(label='Quality Check')
frame_assembly = Transition(label='Frame Assembly')
motor_install = Transition(label='Motor Installation')
sensor_setup = Transition(label='Sensor Setup')
control_unit = Transition(label='Control Unit')
firmware_upload = Transition(label='Firmware Upload')
system_calibration = Transition(label='System Calibration')
flight_testing = Transition(label='Flight Testing')
error_correction = Transition(label='Error Correction')
cosmetic_finish = Transition(label='Cosmetic Finish')
packaging_prep = Transition(label='Packaging Prep')
user_manual = Transition(label='User Manual')
client_training = Transition(label='Client Training')
support_scheduling = Transition(label='Support Scheduling')

skip = SilentTransition()

loop_flight_testing = OperatorPOWL(operator=Operator.LOOP, children=[flight_testing, error_correction])
xor_client_training = OperatorPOWL(operator=Operator.XOR, children=[client_training, support_scheduling])
xor_flight_testing = OperatorPOWL(operator=Operator.XOR, children=[flight_testing, error_correction])
xor_cosmetic_finish = OperatorPOWL(operator=Operator.XOR, children=[cosmetic_finish, packaging_prep])

root = StrictPartialOrder(nodes=[req_analysis, comp_sourcing, quality_check, frame_assembly, motor_install, sensor_setup, control_unit, firmware_upload, system_calibration, loop_flight_testing, xor_client_training, xor_flight_testing, xor_cosmetic_finish, user_manual])
root.order.add_edge(req_analysis, comp_sourcing)
root.order.add_edge(comp_sourcing, quality_check)
root.order.add_edge(quality_check, frame_assembly)
root.order.add_edge(frame_assembly, motor_install)
root.order.add_edge(frame_assembly, sensor_setup)
root.order.add_edge(frame_assembly, control_unit)
root.order.add_edge(control_unit, firmware_upload)
root.order.add_edge(firmware_upload, system_calibration)
root.order.add_edge(system_calibration, loop_flight_testing)
root.order.add_edge(loop_flight_testing, xor_client_training)
root.order.add_edge(loop_flight_testing, xor_flight_testing)
root.order.add_edge(xor_flight_testing, flight_testing)
root.order.add_edge(xor_flight_testing, error_correction)
root.order.add_edge(xor_client_training, client_training)
root.order.add_edge(xor_client_training, support_scheduling)
root.order.add_edge(xor_cosmetic_finish, cosmetic_finish)
root.order.add_edge(xor_cosmetic_finish, packaging_prep)
root.order.add_edge(packaging_prep, user_manual)