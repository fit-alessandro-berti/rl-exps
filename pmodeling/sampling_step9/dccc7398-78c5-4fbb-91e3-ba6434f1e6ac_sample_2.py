import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
Site_Survey = Transition(label='Site Survey')
Design_Draft = Transition(label='Design Draft')
Permit_Review = Transition(label='Permit Review')
Structure_Build = Transition(label='Structure Build')
Enviro_Setup = Transition(label='Enviro Setup')
Nutrient_Mix = Transition(label='Nutrient Mix')
Seed_Selection = Transition(label='Seed Selection')
Plant_Robots = Transition(label='Plant Robots')
Sensor_Install = Transition(label='Sensor Install')
Data_Sync = Transition(label='Data Sync')
Growth_Monitor = Transition(label='Growth Monitor')
Pest_Control = Transition(label='Pest Control')
Harvest_Plan = Transition(label='Harvest Plan')
Quality_Check = Transition(label='Quality Check')
Market_Launch = Transition(label='Market Launch')
Feedback_Loop = Transition(label='Feedback Loop')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Site_Survey, Design_Draft, Permit_Review, Structure_Build, Enviro_Setup, Nutrient_Mix, Seed_Selection, Plant_Robots, Sensor_Install, Data_Sync, Growth_Monitor, Pest_Control, Harvest_Plan, Quality_Check, Market_Launch, Feedback_Loop])
xor = OperatorPOWL(operator=Operator.XOR, children=[skip])
root = StrictPartialOrder(nodes=[loop, xor])
root.order.add_edge(loop, xor)