import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
permit_filing = Transition(label='Permit Filing')
load_testing = Transition(label='Load Testing')
soil_sampling = Transition(label='Soil Sampling')
water_testing = Transition(label='Water Testing')
system_design = Transition(label='System Design')
solar_setup = Transition(label='Solar Setup')
crop_planning = Transition(label='Crop Planning')
stakeholder_meet = Transition(label='Stakeholder Meet')
material_order = Transition(label='Material Order')
system_install = Transition(label='System Install')
environmental_audit = Transition(label='Environmental Audit')
growth_monitoring = Transition(label='Growth Monitoring')
pest_control = Transition(label='Pest Control')
market_launch = Transition(label='Market Launch')

skip = SilentTransition()

loop_site_survey = OperatorPOWL(operator=Operator.LOOP, children=[site_survey, permit_filing])
xor_soil_water = OperatorPOWL(operator=Operator.XOR, children=[soil_sampling, water_testing])
loop_load_soil_water = OperatorPOWL(operator=Operator.LOOP, children=[load_testing, xor_soil_water])
xor_system_design_solar = OperatorPOWL(operator=Operator.XOR, children=[system_design, solar_setup])
loop_load_soil_water_system_design_solar = OperatorPOWL(operator=Operator.LOOP, children=[load_testing, xor_system_design_solar])
xor_crop_planning_stakeholder = OperatorPOWL(operator=Operator.XOR, children=[crop_planning, stakeholder_meet])
loop_load_soil_water_system_design_solar_crop_planning_stakeholder = OperatorPOWL(operator=Operator.LOOP, children=[load_testing, xor_crop_planning_stakeholder])
xor_material_order_system_install = OperatorPOWL(operator=Operator.XOR, children=[material_order, system_install])
loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install = OperatorPOWL(operator=Operator.LOOP, children=[load_testing, xor_material_order_system_install])
xor_environmental_audit_growth_monitoring = OperatorPOWL(operator=Operator.XOR, children=[environmental_audit, growth_monitoring])
loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install_environmental_audit_growth_monitoring = OperatorPOWL(operator=Operator.LOOP, children=[load_testing, xor_environmental_audit_growth_monitoring])
xor_pest_control_market_launch = OperatorPOWL(operator=Operator.XOR, children=[pest_control, market_launch])
loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install_environmental_audit_growth_monitoring_pest_control_market_launch = OperatorPOWL(operator=Operator.LOOP, children=[load_testing, xor_pest_control_market_launch])

root = StrictPartialOrder(nodes=[loop_site_survey, loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install_environmental_audit_growth_monitoring_pest_control_market_launch])
root.order.add_edge(loop_site_survey, xor_soil_water)
root.order.add_edge(xor_soil_water, loop_load_soil_water)
root.order.add_edge(loop_load_soil_water, xor_system_design_solar)
root.order.add_edge(xor_system_design_solar, loop_load_soil_water_system_design_solar)
root.order.add_edge(loop_load_soil_water_system_design_solar, xor_crop_planning_stakeholder)
root.order.add_edge(xor_crop_planning_stakeholder, loop_load_soil_water_system_design_solar_crop_planning_stakeholder)
root.order.add_edge(loop_load_soil_water_system_design_solar_crop_planning_stakeholder, xor_material_order_system_install)
root.order.add_edge(xor_material_order_system_install, loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install)
root.order.add_edge(loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install, xor_environmental_audit_growth_monitoring)
root.order.add_edge(xor_environmental_audit_growth_monitoring, loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install_environmental_audit_growth_monitoring)
root.order.add_edge(loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install_environmental_audit_growth_monitoring, xor_pest_control_market_launch)
root.order.add_edge(xor_pest_control_market_launch, loop_load_soil_water_system_design_solar_crop_planning_stakeholder_material_order_system_install_environmental_audit_growth_monitoring_pest_control_market_launch)