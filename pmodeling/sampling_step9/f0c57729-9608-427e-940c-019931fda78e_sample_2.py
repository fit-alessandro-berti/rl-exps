import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
inquiry = Transition(label='Inquiry Intake')
consultation = Transition(label='Consultation Call')
concept = Transition(label='Concept Draft')
feedback = OperatorPOWL(operator=Operator.XOR, children=[Transition(label='Feedback Loop'), SilentTransition()])
contract = Transition(label='Contract Setup')
artist = Transition(label='Artist Match')
material = Transition(label='Material Sourcing')
ethics = Transition(label='Ethics Review')
progress = Transition(label='Progress Check')
milestone = OperatorPOWL(operator=Operator.XOR, children=[Transition(label='Milestone Approve'), SilentTransition()])
audit = Transition(label='Quality Audit')
copyright = Transition(label='Copyright Transfer')
packaging = Transition(label='Packaging Plan')
shipping = Transition(label='Shipping Arrange')
post_delivery = Transition(label='Post Delivery')
survey = Transition(label='Client Survey')
skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[concept, feedback])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[artist, material, ethics, progress, milestone, audit, copyright, packaging, shipping, post_delivery, survey])
root = StrictPartialOrder(nodes=[inquiry, consultation, loop1, contract, loop2])
root.order.add_edge(inquiry, consultation)
root.order.add_edge(consultation, loop1)
root.order.add_edge(loop1, loop2)
root.order.add_edge(loop2, contract)
root.order.add_edge(contract, loop2)