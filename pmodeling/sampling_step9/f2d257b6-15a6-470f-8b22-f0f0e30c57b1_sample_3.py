import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
artifact_intake = Transition(label='Artifact Intake')
condition_check = Transition(label='Condition Check')
material_sampling = Transition(label='Material Sampling')
radiocarbon_test = Transition(label='Radiocarbon Test')
provenance_review = Transition(label='Provenance Review')
imaging_capture = Transition(label='Imaging Capture')
chemical_analysis = Transition(label='Chemical Analysis')
historical_match = Transition(label='Historical Match')
expert_consult = Transition(label='Expert Consult')
forgeries_scan = Transition(label='Forgery Scan')
market_survey = Transition(label='Market Survey')
value_estimate = Transition(label='Value Estimate')
certification = Transition(label='Certification')
digital_archive = Transition(label='Digital Archive')
final_storage = Transition(label='Final Storage')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[artifact_intake, condition_check])
xor1 = OperatorPOWL(operator=Operator.XOR, children=[material_sampling, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[radiocarbon_test, provenance_review])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[imaging_capture, chemical_analysis])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[historical_match, expert_consult])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[forgeries_scan, market_survey])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[value_estimate, certification])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[digital_archive, final_storage])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[xor1, xor2, xor3, xor4, xor5, xor6, xor7])
root = StrictPartialOrder(nodes=[loop, xor8])
root.order.add_edge(loop, xor8)