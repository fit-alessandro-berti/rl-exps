xor10 = OperatorPOWL(operator=Operator.XOR, children=[Package_Prep, skip])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[Doc_Compile, skip])
xor12 = OperatorPOWL(operator=Operator.XOR, children=[Ship_Arrange, skip])
xor13 = OperatorPOWL(operator=Operator.XOR, children=[Remote_Setup, skip])
root = StrictPartialOrder(nodes=[loop, xor, xor2, xor3, xor4, xor5, xor6, xor7, xor8, xor9, xor10, xor11, xor12, xor13])
root.order.add_edge(loop, xor)
