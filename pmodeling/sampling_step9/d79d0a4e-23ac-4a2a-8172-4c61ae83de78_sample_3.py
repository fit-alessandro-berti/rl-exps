Package_Prep = Transition(label='Package Prep')
Doc_Compile = Transition(label='Doc Compile')
Ship_Arrange = Transition(label='Ship Arrange')
Remote_Setup = Transition(label='Remote Setup')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Spec_Review, Component_Pick])
xor = OperatorPOWL(operator=Operator.XOR, children=[Frame_Build, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[Motor_Mount, skip])
