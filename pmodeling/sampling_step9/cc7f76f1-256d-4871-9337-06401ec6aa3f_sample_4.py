import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
intel_gathering = Transition(label='Intel Gathering')
risk_assess = Transition(label='Risk Assess')
behavior_scan = Transition(label='Behavior Scan')
network_monitor = Transition(label='Network Monitor')
audit_conduct = Transition(label='Audit Conduct')
flag_suspicion = Transition(label='Flag Suspicion')
legal_review = Transition(label='Legal Review')
compliance_check = Transition(label='Compliance Check')
employee_train = Transition(label='Employee Train')
threat_simulate = Transition(label='Threat Simulate')
incident_plan = Transition(label='Incident Plan')
response_drill = Transition(label='Response Drill')
data_encrypt = Transition(label='Data Encrypt')
partner_liaison = Transition(label='Partner Liaison')
report_generate = Transition(label='Report Generate')
feedback_loop = Transition(label='Feedback Loop')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[audit_conduct, behavior_scan, network_monitor])
xor = OperatorPOWL(operator=Operator.XOR, children=[flag_suspicion, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[legal_review, compliance_check])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[employee_train, threat_simulate])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[incident_plan, response_drill])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[data_encrypt, partner_liaison])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[report_generate, feedback_loop])
root = StrictPartialOrder(nodes=[loop, xor, xor2, xor3, xor4, xor5, xor6])
root.order.add_edge(loop, xor)
root.order.add_edge(loop, xor2)
root.order.add_edge(loop, xor3)
root.order.add_edge(loop, xor4)
root.order.add_edge(loop, xor5)
root.order.add_edge(loop, xor6)