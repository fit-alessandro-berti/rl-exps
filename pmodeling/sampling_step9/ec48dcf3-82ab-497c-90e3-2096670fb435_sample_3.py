import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
SiteSurvey = Transition(label='Site Survey')
DesignLayout = Transition(label='Design Layout')
StructureBuild = Transition(label='Structure Build')
SystemInstall = Transition(label='System Install')
ClimateSetup = Transition(label='Climate Setup')
NutrientPrep = Transition(label='Nutrient Prep')
SeedGerminate = Transition(label='Seed Germinate')
PlantingPhase = Transition(label='Planting Phase')
SensorDeploy = Transition(label='Sensor Deploy')
PestControl = Transition(label='Pest Control')
WaterMonitor = Transition(label='Water Monitor')
DataAnalyze = Transition(label='Data Analyze')
StaffTrain = Transition(label='Staff Train')
YieldForecast = Transition(label='Yield Forecast')
CommunityMeet = Transition(label='Community Meet')
skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[SiteSurvey, DesignLayout])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[StructureBuild, SystemInstall])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[ClimateSetup, NutrientPrep])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[SeedGerminate, PlantingPhase])
loop5 = OperatorPOWL(operator=Operator.LOOP, children=[SensorDeploy, PestControl])
loop6 = OperatorPOWL(operator=Operator.LOOP, children=[WaterMonitor, DataAnalyze])
loop7 = OperatorPOWL(operator=Operator.LOOP, children=[StaffTrain, YieldForecast])
loop8 = OperatorPOWL(operator=Operator.LOOP, children=[CommunityMeet, skip])
root = StrictPartialOrder(nodes=[loop1, loop2, loop3, loop4, loop5, loop6, loop7, loop8])
root.order.add_edge(loop1, loop2)
root.order.add_edge(loop2, loop3)
root.order.add_edge(loop3, loop4)
root.order.add_edge(loop4, loop5)
root.order.add_edge(loop5, loop6)
root.order.add_edge(loop6, loop7)
root.order.add_edge(loop7, loop8)
root.order.add_edge(loop8, loop1)