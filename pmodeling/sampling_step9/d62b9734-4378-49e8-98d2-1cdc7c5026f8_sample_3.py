import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
Provenance_Check = Transition(label='Provenance Check')
Image_Capture = Transition(label='Image Capture')
Material_Scan = Transition(label='Material Scan')
Expert_Review = Transition(label='Expert Review')
Historical_Cross = Transition(label='Historical Cross')
Legal_Verify = Transition(label='Legal Verify')
Registry_Search = Transition(label='Registry Search')
Customs_Clear = Transition(label='Customs Clear')
Condition_Assess = Transition(label='Condition Assess')
Data_Log = Transition(label='Data Log')
Chain_Custody = Transition(label='Chain Custody')
Report_Draft = Transition(label='Report Draft')
Certification = Transition(label='Certification')
Secure_Archive = Transition(label='Secure Archive')
Auction_Prep = Transition(label='Auction Prep')
skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[Provenance_Check, Image_Capture, Material_Scan, Expert_Review, Historical_Cross, Legal_Verify, Registry_Search, Customs_Clear, Condition_Assess])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[Data_Log, Chain_Custody, Report_Draft, Certification, Secure_Archive, Auction_Prep])
root = StrictPartialOrder(nodes=[loop1, loop2])
root.order.add_edge(loop1, loop2)