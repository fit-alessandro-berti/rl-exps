import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
IngredientSourcing = Transition(label='Ingredient Sourcing')
BotanicalExtraction = Transition(label='Botanical Extraction')
InitialBlending = Transition(label='Initial Blending')
SensoryTesting = Transition(label='Sensory Testing')
ChemicalAnalysis = Transition(label='Chemical Analysis')
RecipeRefinement = Transition(label='Recipe Refinement')
StabilityCheck = Transition(label='Stability Check')
ClientSampling = Transition(label='Client Sampling')
FeedbackReview = Transition(label='Feedback Review')
FinalAdjustment = Transition(label='Final Adjustment')
CustomPackaging = Transition(label='Custom Packaging')
LabelDesign = Transition(label='Label Design')
HandLabeling = Transition(label='Hand Labeling')
RegulatoryAudit = Transition(label='Regulatory Audit')
BatchDocumentation = Transition(label='Batch Documentation')
LimitedRelease = Transition(label='Limited Release')
MarketLaunch = Transition(label='Market Launch')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[IngredientSourcing, BotanicalExtraction, InitialBlending, SensoryTesting, ChemicalAnalysis, RecipeRefinement, StabilityCheck, ClientSampling, FeedbackReview, FinalAdjustment, CustomPackaging, LabelDesign, HandLabeling, RegulatoryAudit, BatchDocumentation, LimitedRelease, MarketLaunch])
root = StrictPartialOrder(nodes=[loop])
root.order.add_edge(loop, IngredientSourcing)
root.order.add_edge(IngredientSourcing, BotanicalExtraction)
root.order.add_edge(BotanicalExtraction, InitialBlending)
root.order.add_edge(InitialBlending, SensoryTesting)
root.order.add_edge(SensoryTesting, ChemicalAnalysis)
root.order.add_edge(ChemicalAnalysis, RecipeRefinement)
root.order.add_edge(RecipeRefinement, StabilityCheck)
root.order.add_edge(StabilityCheck, ClientSampling)
root.order.add_edge(ClientSampling, FeedbackReview)
root.order.add_edge(FeedbackReview, FinalAdjustment)
root.order.add_edge(FinalAdjustment, CustomPackaging)
root.order.add_edge(CustomPackaging, LabelDesign)
root.order.add_edge(LabelDesign, HandLabeling)
root.order.add_edge(HandLabeling, RegulatoryAudit)
root.order.add_edge(RegulatoryAudit, BatchDocumentation)
root.order.add_edge(BatchDocumentation, LimitedRelease)
root.order.add_edge(LimitedRelease, MarketLaunch)