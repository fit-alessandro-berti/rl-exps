import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

seed_selection = Transition(label='Seed Selection')
germination_start = Transition(label='Germination Start')
seedling_transplant = Transition(label='Seedling Transplant')
nutrient_mix = Transition(label='Nutrient Mix')
water_delivery = Transition(label='Water Delivery')
light_adjustment = Transition(label='Light Adjustment')
climate_control = Transition(label='Climate Control')
sensor_monitoring = Transition(label='Sensor Monitoring')
health_check = Transition(label='Health Check')
pest_control = Transition(label='Pest Control')
harvest_planning = Transition(label='Harvest Planning')
crop_harvest = Transition(label='Crop Harvest')
waste_sorting = Transition(label='Waste Sorting')
biomass_process = Transition(label='Biomass Process')
energy_recycling = Transition(label='Energy Recycling')
data_analysis = Transition(label='Data Analysis')
yield_forecast = Transition(label='Yield Forecast')

skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[seed_selection, germination_start, seedling_transplant, nutrient_mix, water_delivery, light_adjustment, climate_control, sensor_monitoring])
xor1 = OperatorPOWL(operator=Operator.XOR, children=[health_check, pest_control])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[harvest_planning, crop_harvest])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[waste_sorting, biomass_process])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[energy_recycling, data_analysis])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[yield_forecast])

root = StrictPartialOrder(nodes=[loop, xor1, xor2, xor3, xor4, xor5])
root.order.add_edge(loop, xor1)
root.order.add_edge(loop, xor2)
root.order.add_edge(loop, xor3)
root.order.add_edge(loop, xor4)
root.order.add_edge(loop, xor5)
root.order.add_edge(xor1, xor2)
root.order.add_edge(xor1, xor3)
root.order.add_edge(xor1, xor4)
root.order.add_edge(xor1, xor5)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor2, xor4)
root.order.add_edge(xor2, xor5)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor3, xor5)
root.order.add_edge(xor4, xor5)