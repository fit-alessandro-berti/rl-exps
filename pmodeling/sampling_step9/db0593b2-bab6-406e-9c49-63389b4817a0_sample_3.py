import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

SiteAssess = Transition(label='Site Assess')
EnvAnalysis = Transition(label='Env Analysis')
ModularInstall = Transition(label='Modular Install')
IrrigationSetup = Transition(label='Irrigation Setup')
CropSelection = Transition(label='Crop Selection')
NutrientMix = Transition(label='Nutrient Mix')
LightingCalibrate = Transition(label='Lighting Calibrate')
PestMonitor = Transition(label='Pest Monitor')
StaffTraining = Transition(label='Staff Training')
EnergyIntegrate = Transition(label='Energy Integrate')
DataAnalytics = Transition(label='Data Analytics')
WasteRecycle = Transition(label='Waste Recycle')
MarketDevelop = Transition(label='Market Develop')
YieldOptimize = Transition(label='Yield Optimize')
ClimateSimulate = Transition(label='Climate Simulate')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[SiteAssess, EnvAnalysis])
xor = OperatorPOWL(operator=Operator.XOR, children=[ModularInstall, IrrigationSetup])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[CropSelection, NutrientMix])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[LightingCalibrate, PestMonitor])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[StaffTraining, EnergyIntegrate])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[DataAnalytics, WasteRecycle])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[MarketDevelop, YieldOptimize])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[ClimateSimulate, skip])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[xor7, xor6])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[xor5, xor8])
xor10 = OperatorPOWL(operator=Operator.XOR, children=[xor4, xor9])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[xor3, xor10])
xor12 = OperatorPOWL(operator=Operator.XOR, children=[xor2, xor11])
root = StrictPartialOrder(nodes=[loop, xor12])
root.order.add_edge(loop, xor12)