import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

site_survey = Transition(label='Site Survey')
env_analysis = Transition(label='Env Analysis')
structure_build = Transition(label='Structure Build')
hydroponics_fit = Transition(label='Hydroponics Fit')
nutrient_mix = Transition(label='Nutrient Mix')
climate_setup = Transition(label='Climate Setup')
energy_audit = Transition(label='Energy Audit')
crop_select = Transition(label='Crop Select')
pest_control = Transition(label='Pest Control')
growth_monitor = Transition(label='Growth Monitor')
harvest_plan = Transition(label='Harvest Plan')
waste_recycle = Transition(label='Waste Recycle')
community_meet = Transition(label='Community Meet')
supply_sync = Transition(label='Supply Sync')
data_review = Transition(label='Data Review')

skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[site_survey, env_analysis])
xor = OperatorPOWL(operator=Operator.XOR, children=[structure_build, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[hydroponics_fit, xor])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[nutrient_mix, xor2])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[climate_setup, xor3])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[energy_audit, xor4])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[crop_select, xor5])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[pest_control, xor6])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[growth_monitor, xor7])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[harvest_plan, xor8])
xor10 = OperatorPOWL(operator=Operator.XOR, children=[waste_recycle, xor9])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[community_meet, xor10])
xor12 = OperatorPOWL(operator=Operator.XOR, children=[supply_sync, xor11])
xor13 = OperatorPOWL(operator=Operator.XOR, children=[data_review, xor12])
root = StrictPartialOrder(nodes=[loop, xor13])
root.order.add_edge(loop, xor13)