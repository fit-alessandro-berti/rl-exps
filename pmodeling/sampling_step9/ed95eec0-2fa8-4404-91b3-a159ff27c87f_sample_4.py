import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

Trend_Scan = Transition(label='Trend Scan')
Idea_Harvest = Transition(label='Idea Harvest')
Sector_Match = Transition(label='Sector Match')
Brainstorm_Hub = Transition(label='Brainstorm Hub')
Concept_Filter = Transition(label='Concept Filter')
Prototype_Build = Transition(label='Prototype Build')
Hybrid_Testing = Transition(label='Hybrid Testing')
Stakeholder_Sync = Transition(label='Stakeholder Sync')
Risk_Assess = Transition(label='Risk Assess')
Scenario_Map = Transition(label='Scenario Map')
Strategy_Align = Transition(label='Strategy Align')
Pilot_Launch = Transition(label='Pilot Launch')
Data_Capture = Transition(label='Data Capture')
Market_Sense = Transition(label='Market Sense')
Scale_Plan = Transition(label='Scale Plan')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[Trend_Scan, Idea_Harvest])
xor = OperatorPOWL(operator=Operator.XOR, children=[Sector_Match, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[Brainstorm_Hub, skip])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[Concept_Filter, skip])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[Prototype_Build, skip])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[Hybrid_Testing, skip])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[Stakeholder_Sync, skip])
xor7 = OperatorPOWL(operator=Operator.XOR, children=[Risk_Assess, skip])
xor8 = OperatorPOWL(operator=Operator.XOR, children=[Scenario_Map, skip])
xor9 = OperatorPOWL(operator=Operator.XOR, children=[Strategy_Align, skip])
xor10 = OperatorPOWL(operator=Operator.XOR, children=[Pilot_Launch, skip])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[Data_Capture, skip])
xor12 = OperatorPOWL(operator=Operator.XOR, children=[Market_Sense, skip])
xor13 = OperatorPOWL(operator=Operator.XOR, children=[Scale_Plan, skip])
root = StrictPartialOrder(nodes=[loop, xor, xor2, xor3, xor4, xor5, xor6, xor7, xor8, xor9, xor10, xor11, xor12, xor13])
root.order.add_edge(loop, xor)
root.order.add_edge(xor, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, xor5)
root.order.add_edge(xor5, xor6)
root.order.add_edge(xor6, xor7)
root.order.add_edge(xor7, xor8)
root.order.add_edge(xor8, xor9)
root.order.add_edge(xor9, xor10)
root.order.add_edge(xor10, xor11)
root.order.add_edge(xor11, xor12)
root.order.add_edge(xor12, xor13)