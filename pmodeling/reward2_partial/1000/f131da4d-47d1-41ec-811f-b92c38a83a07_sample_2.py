import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

milk_sourcing = Transition(label='Milk Sourcing')
culture_selection = Transition(label='Culture Selection')
milk_testing = Transition(label='Milk Testing')
fermentation_start = Transition(label='Fermentation Start')
temperature_control = Transition(label='Temperature Control')
pH_monitoring = Transition(label='pH Monitoring')
curd_cutting = Transition(label='Curd Cutting')
whey_draining = Transition(label='Whey Draining')
molding_cheese = Transition(label='Molding Cheese')
salting_process = Transition(label='Salting Process')
aging_setup = Transition(label='Aging Setup')
quality_check = Transition(label='Quality Check')
packaging_prep = Transition(label='Packaging Prep')
label_design = Transition(label='Label Design')
distribution_plan = Transition(label='Distribution Plan')
retail_delivery = Transition(label='Retail Delivery')
customer_feedback = Transition(label='Customer Feedback')

skip = SilentTransition()

milk_sourcing_to_culture_selection = OperatorPOWL(operator=Operator.XOR, children=[milk_sourcing, culture_selection])
culture_selection_to_milk_testing = OperatorPOWL(operator=Operator.XOR, children=[culture_selection, milk_testing])
milk_testing_to_fermentation_start = OperatorPOWL(operator=Operator.XOR, children=[milk_testing, fermentation_start])
fermentation_start_to_temperature_control = OperatorPOWL(operator=Operator.XOR, children=[fermentation_start, temperature_control])
temperature_control_to_pH_monitoring = OperatorPOWL(operator=Operator.XOR, children=[temperature_control, pH_monitoring])
pH_monitoring_to_curd_cutting = OperatorPOWL(operator=Operator.XOR, children=[pH_monitoring, curd_cutting])
curd_cutting_to_whey_draining = OperatorPOWL(operator=Operator.XOR, children=[curd_cutting, whey_draining])
whey_draining_to_molding_cheese = OperatorPOWL(operator=Operator.XOR, children=[whey_draining, molding_cheese])
molding_cheese_to_salting_process = OperatorPOWL(operator=Operator.XOR, children=[molding_cheese, salting_process])
salting_process_to_aging_setup = OperatorPOWL(operator=Operator.XOR, children=[salting_process, aging_setup])
aging_setup_to_quality_check = OperatorPOWL(operator=Operator.XOR, children=[aging_setup, quality_check])
quality_check_to_packaging_prep = OperatorPOWL(operator=Operator.XOR, children=[quality_check, packaging_prep])
packaging_prep_to_label_design = OperatorPOWL(operator=Operator.XOR, children=[packaging_prep, label_design])
label_design_to_distribution_plan = OperatorPOWL(operator=Operator.XOR, children=[label_design, distribution_plan])
distribution_plan_to_retail_delivery = OperatorPOWL(operator=Operator.XOR, children=[distribution_plan, retail_delivery])
retail_delivery_to_customer_feedback = OperatorPOWL(operator=Operator.XOR, children=[retail_delivery, customer_feedback])

root = StrictPartialOrder(nodes=[milk_sourcing_to_culture_selection, culture_selection_to_milk_testing, milk_testing_to_fermentation_start, fermentation_start_to_temperature_control, temperature_control_to_pH_monitoring, pH_monitoring_to_curd_cutting, curd_cutting_to_whey_draining, whey_draining_to_molding_cheese, molding_cheese_to_salting_process, salting_process_to_aging_setup, aging_setup_to_quality_check, quality_check_to_packaging_prep, packaging_prep_to_label_design, label_design_to_distribution_plan, distribution_plan_to_retail_delivery, retail_delivery_to_customer_feedback])
root.order.add_edge(milk_sourcing_to_culture_selection, culture_selection_to_milk_testing)
root.order.add_edge(culture_selection_to_milk_testing, milk_testing_to_fermentation_start)
root.order.add_edge(milk_testing_to_fermentation_start, fermentation_start_to_temperature_control)
root.order.add_edge(fermentation_start_to_temperature_control, temperature_control_to_pH_monitoring)
root.order.add_edge(temperature_control_to_pH_monitoring, pH_monitoring_to_curd_cutting)
root.order.add_edge(pH_monitoring_to_curd_cutting, curd_cutting_to_whey_draining)
root.order.add_edge(curd_cutting_to_whey_draining, whey_draining_to_molding_cheese)
root.order.add_edge(whey_draining_to_molding_cheese, molding_cheese_to_salting_process)
root.order.add_edge(molding_cheese_to_salting_process, salting_process_to_aging_setup)
root.order.add_edge(salting_process_to_aging_setup, aging_setup_to_quality_check)
root.order.add_edge(aging_setup_to_quality_check, quality_check_to_packaging_prep)
root.order.add_edge(quality_check_to_packaging_prep, packaging_prep_to_label_design)
root.order.add_edge(packaging_prep_to_label_design, label_design_to_distribution_plan)
root.order.add_edge(label_design_to_distribution_plan, distribution_plan_to_retail_delivery)
root.order.add_edge(distribution_plan_to_retail_delivery, retail_delivery_to_customer_feedback)