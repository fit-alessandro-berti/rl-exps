import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
opportunity_scan = Transition(label='Opportunity Scan')
idea_workshop = Transition(label='Idea Workshop')
concept_merge = Transition(label='Concept Merge')
resource_align = Transition(label='Resource Align')
prototype_build = Transition(label='Prototype Build')
feasibility_test = Transition(label='Feasibility Test')
pilot_launch = Transition(label='Pilot Launch')
feedback_gather = Transition(label='Feedback Gather')
design_adapt = Transition(label='Design Adapt')
compliance_check = Transition(label='Compliance Check')
scaling_plan = Transition(label='Scaling Plan')
ip_management = Transition(label='IP Management')
market_sync = Transition(label='Market Sync')
partner_review = Transition(label='Partner Review')
exit_strategy = Transition(label='Exit Strategy')
skip = SilentTransition()
loop = OperatorPOWL(operator=Operator.LOOP, children=[idea_workshop, feasibility_test, pilot_launch, feedback_gather, design_adapt, compliance_check, scaling_plan, ip_management, market_sync, partner_review])
xor = OperatorPOWL(operator=Operator.XOR, children=[prototype_build, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[concept_merge, resource_align])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[opportunity_scan, xor2])
root = StrictPartialOrder(nodes=[loop, xor3])
root.order.add_edge(loop, xor)
root.order.add_edge(xor3, xor)
root.order.add_edge(xor, loop)
root.order.add_edge(xor2, loop)
root.order.add_edge(xor3, loop)