import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
Milk_Sourcing = Transition(label='Milk Sourcing')
Quality_Testing = Transition(label='Quality Testing')
Milk_Pasteurize = Transition(label='Milk Pasteurize')
Culture_Inoculate = Transition(label='Culture Inoculate')
Coagulation = Transition(label='Coagulation')
Curd_Cutting = Transition(label='Curd Cutting')
Whey_Drain = Transition(label='Whey Drain')
Pressing = Transition(label='Pressing')
Salting = Transition(label='Salting')
Aging_Control = Transition(label='Aging Control')
Sensory_Audit = Transition(label='Sensory Audit')
Packaging_Design = Transition(label='Packaging Design')
Label_Approval = Transition(label='Label Approval')
Order_Customization = Transition(label='Order Customization')
Logistics_Plan = Transition(label='Logistics Plan')
Market_Delivery = Transition(label='Market Delivery')
Customer_Feedback = Transition(label='Customer Feedback')
Regulatory_Check = Transition(label='Regulatory Check')
skip = SilentTransition()
milk_sourcing_loop = OperatorPOWL(operator=Operator.LOOP, children=[Milk_Sourcing, Quality_Testing, Milk_Pasteurize, Culture_Inoculate, Coagulation, Curd_Cutting, Whey_Drain, Pressing, Salting, Aging_Control])
sensory_audit_loop = OperatorPOWL(operator=Operator.LOOP, children=[Sensory_Audit])
packaging_design_loop = OperatorPOWL(operator=Operator.LOOP, children=[Packaging_Design, Label_Approval])
order_customization_loop = OperatorPOWL(operator=Operator.LOOP, children=[Order_Customization])
logistics_plan_loop = OperatorPOWL(operator=Operator.LOOP, children=[Logistics_Plan])
market_delivery_loop = OperatorPOWL(operator=Operator.LOOP, children=[Market_Delivery])
customer_feedback_loop = OperatorPOWL(operator=Operator.LOOP, children=[Customer_Feedback])
regulatory_check_loop = OperatorPOWL(operator=Operator.LOOP, children=[Regulatory_Check])
xor = OperatorPOWL(operator=Operator.XOR, children=[milk_sourcing_loop, sensory_audit_loop, packaging_design_loop, order_customization_loop, logistics_plan_loop, market_delivery_loop, customer_feedback_loop, regulatory_check_loop])
root = StrictPartialOrder(nodes=[xor])
root.order.add_edge(milk_sourcing_loop, sensory_audit_loop)
root.order.add_edge(sensory_audit_loop, packaging_design_loop)
root.order.add_edge(packaging_design_loop, order_customization_loop)
root.order.add_edge(order_customization_loop, logistics_plan_loop)
root.order.add_edge(logistics_plan_loop, market_delivery_loop)
root.order.add_edge(market_delivery_loop, customer_feedback_loop)
root.order.add_edge(customer_feedback_loop, regulatory_check_loop)