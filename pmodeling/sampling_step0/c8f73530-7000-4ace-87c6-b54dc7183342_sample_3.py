import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
milk_sourcing = Transition(label='Milk Sourcing')
quality_testing = Transition(label='Quality Testing')
milk_pasturize = Transition(label='Milk Pasteurize')
culture_inoculate = Transition(label='Culture Inoculate')
coagulation = Transition(label='Coagulation')
curd_cutting = Transition(label='Curd Cutting')
whey_drain = Transition(label='Whey Drain')
pressing = Transition(label='Pressing')
salting = Transition(label='Salting')
aging_control = Transition(label='Aging Control')
sensory_audit = Transition(label='Sensory Audit')
packaging_design = Transition(label='Packaging Design')
label_approval = Transition(label='Label Approval')
order_customization = Transition(label='Order Customization')
logistics_plan = Transition(label='Logistics Plan')
market_delivery = Transition(label='Market Delivery')
customer_feedback = Transition(label='Customer Feedback')
regulatory_check = Transition(label='Regulatory Check')
skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[milk_sourcing, quality_testing])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[milk_pasturize, culture_inoculate, coagulation])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[curd_cutting, whey_drain, pressing, salting])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[aging_control, sensory_audit])
loop5 = OperatorPOWL(operator=Operator.LOOP, children=[packaging_design, label_approval, order_customization, logistics_plan])
loop6 = OperatorPOWL(operator=Operator.LOOP, children=[market_delivery, customer_feedback, regulatory_check])
xor1 = OperatorPOWL(operator=Operator.XOR, children=[loop1, skip])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[loop2, xor1])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[loop3, xor2])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[loop4, xor3])
xor5 = OperatorPOWL(operator=Operator.XOR, children=[loop5, xor4])
xor6 = OperatorPOWL(operator=Operator.XOR, children=[loop6, xor5])
root = StrictPartialOrder(nodes=[xor1, xor2, xor3, xor4, xor5, xor6])
root.order.add_edge(xor1, xor2)
root.order.add_edge(xor2, xor3)
root.order.add_edge(xor3, xor4)
root.order.add_edge(xor4, xor5)
root.order.add_edge(xor5, xor6)