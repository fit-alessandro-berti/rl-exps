import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
A = Transition(label='Site Survey')
B = Transition(label='Design Layout')
C = Transition(label='Permit Filing')
D = Transition(label='Module Build')
E = Transition(label='System Install')
F = Transition(label='Climate Setup')
G = Transition(label='Lighting Configure')
H = Transition(label='Irrigation Setup')
I = Transition(label='Nutrient Mix')
J = Transition(label='Pest Check')
K = Transition(label='Sensor Calibrate')
L = Transition(label='Data Integration')
M = Transition(label='Crop Planting')
N = Transition(label='Growth Monitor')
O = Transition(label='Yield Analyze')
P = Transition(label='Waste Manage')
Q = Transition(label='Energy Audit')

skip = SilentTransition()
site_survey_loop = OperatorPOWL(operator=Operator.LOOP, children=[A, B, C])
design_layout_loop = OperatorPOWL(operator=Operator.LOOP, children=[D, E, F, G, H, I, J, K, L])
planting_loop = OperatorPOWL(operator=Operator.LOOP, children=[M, N, O, P, Q])
root = StrictPartialOrder(nodes=[site_survey_loop, design_layout_loop, planting_loop])
root.order.add_edge(site_survey_loop, design_layout_loop)
root.order.add_edge(design_layout_loop, planting_loop)
root.order.add_edge(planting_loop, site_survey_loop)