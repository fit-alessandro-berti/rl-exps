import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator

SiteAssess = Transition(label='Site Assess')
EnvAnalysis = Transition(label='Env Analysis')
ModularInstall = Transition(label='Modular Install')
IrrigationSetup = Transition(label='Irrigation Setup')
CropSelection = Transition(label='Crop Selection')
NutrientMix = Transition(label='Nutrient Mix')
LightingCalibrate = Transition(label='Lighting Calibrate')
PestMonitor = Transition(label='Pest Monitor')
StaffTraining = Transition(label='Staff Training')
EnergyIntegrate = Transition(label='Energy Integrate')
DataAnalytics = Transition(label='Data Analytics')
WasteRecycle = Transition(label='Waste Recycle')
MarketDevelop = Transition(label='Market Develop')
YieldOptimize = Transition(label='Yield Optimize')
ClimateSimulate = Transition(label='Climate Simulate')

skip = SilentTransition()
loop1 = OperatorPOWL(operator=Operator.LOOP, children=[SiteAssess, EnvAnalysis])
xor1 = OperatorPOWL(operator=Operator.XOR, children=[ModularInstall, IrrigationSetup])
loop2 = OperatorPOWL(operator=Operator.LOOP, children=[CropSelection, NutrientMix])
xor2 = OperatorPOWL(operator=Operator.XOR, children=[LightingCalibrate, PestMonitor])
loop3 = OperatorPOWL(operator=Operator.LOOP, children=[StaffTraining, EnergyIntegrate])
xor3 = OperatorPOWL(operator=Operator.XOR, children=[DataAnalytics, WasteRecycle])
loop4 = OperatorPOWL(operator=Operator.LOOP, children=[MarketDevelop, YieldOptimize])
xor4 = OperatorPOWL(operator=Operator.XOR, children=[ClimateSimulate, skip])

root = StrictPartialOrder(nodes=[loop1, xor1, loop2, xor2, loop3, xor3, loop4, xor4])
root.order.add_edge(loop1, xor1)
root.order.add_edge(xor1, ModularInstall)
root.order.add_edge(ModularInstall, IrrigationSetup)
root.order.add_edge(IrrigationSetup, xor1)
root.order.add_edge(loop2, CropSelection)
root.order.add_edge(CropSelection, NutrientMix)
root.order.add_edge(NutrientMix, xor2)
root.order.add_edge(xor2, LightingCalibrate)
root.order.add_edge(LightingCalibrate, PestMonitor)
root.order.add_edge(PestMonitor, xor2)
root.order.add_edge(loop3, StaffTraining)
root.order.add_edge(StaffTraining, EnergyIntegrate)
root.order.add_edge(EnergyIntegrate, xor3)
root.order.add_edge(xor3, DataAnalytics)
root.order.add_edge(DataAnalytics, WasteRecycle)
root.order.add_edge(WasteRecycle, xor3)
root.order.add_edge(loop4, MarketDevelop)
root.order.add_edge(MarketDevelop, YieldOptimize)
root.order.add_edge(YieldOptimize, xor4)
root.order.add_edge(xor4, ClimateSimulate)