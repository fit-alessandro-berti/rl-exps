import pm4py
from pm4py.objects.powl.obj import StrictPartialOrder, OperatorPOWL, Transition, SilentTransition
from pm4py.objects.process_tree.obj import Operator
MilkSourcing = Transition(label='Milk Sourcing')
QualityTesting = Transition(label='Quality Testing')
CulturePrep = Transition(label='Culture Prep')
MilkPasteurize = Transition(label='Milk Pasteurize')
CurdCutting = Transition(label='Curd Cutting')
WheyDraining = Transition(label='Whey Draining')
MoldingCheese = Transition(label='Molding Cheese')
PressingBlocks = Transition(label='Pressing Blocks')
SaltingProcess = Transition(label='Salting Process')
AgingMonitoring = Transition(label='Aging Monitoring')
FlavorProfiling = Transition(label='Flavor Profiling')
PackagingDesign = Transition(label='Packaging Design')
ComplianceCheck = Transition(label='Compliance Check')
MarketResearch = Transition(label='Market Research')
DirectShipping = Transition(label='Direct Shipping')
CustomerFeedback = Transition(label='Customer Feedback')
RecipeAdjust = Transition(label='Recipe Adjust')

skip = SilentTransition()

milk_sourcing = OperatorPOWL(operator=Operator.CONCURRENT, children=[MilkSourcing, QualityTesting])
culture_prep = OperatorPOWL(operator=Operator.CONCURRENT, children=[CulturePrep, MilkPasteurize])
curd_cutting = OperatorPOWL(operator=Operator.CONCURRENT, children=[CurdCutting, WheyDraining])
molding_cheese = OperatorPOWL(operator=Operator.CONCURRENT, children=[MoldingCheese, PressingBlocks])
salting_process = OperatorPOWL(operator=Operator.CONCURRENT, children=[SaltingProcess, AgingMonitoring])
flavor_profiling = OperatorPOWL(operator=Operator.CONCURRENT, children=[FlavorProfiling, PackagingDesign])
compliance_check = OperatorPOWL(operator=Operator.CONCURRENT, children=[ComplianceCheck, MarketResearch])
direct_shipping = OperatorPOWL(operator=Operator.CONCURRENT, children=[DirectShipping, CustomerFeedback])
recipe_adjust = OperatorPOWL(operator=Operator.CONCURRENT, children=[RecipeAdjust, skip])

root = StrictPartialOrder(nodes=[milk_sourcing, culture_prep, curd_cutting, molding_cheese, salting_process, flavor_profiling, compliance_check, direct_shipping, recipe_adjust])
root.order.add_edge(milk_sourcing, culture_prep)
root.order.add_edge(culture_prep, curd_cutting)
root.order.add_edge(curd_cutting, molding_cheese)
root.order.add_edge(molding_cheese, salting_process)
root.order.add_edge(salting_process, flavor_profiling)
root.order.add_edge(flavor_profiling, compliance_check)
root.order.add_edge(compliance_check, direct_shipping)
root.order.add_edge(direct_shipping, recipe_adjust)