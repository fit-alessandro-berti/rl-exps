xor10 = OperatorPOWL(operator=Operator.XOR, children=[license_setup, skip])
xor11 = OperatorPOWL(operator=Operator.XOR, children=[frame_arrange, skip])

root = StrictPartialOrder(nodes=[loop, xor, xor2, xor3, xor4, xor5, xor6, xor7, xor8, xor9, xor10, xor11])
root.order.add_edge(loop, xor)
root.order.add_edge(xor, xor2)
